<p>The following is an annotated list of online repositories containing images which can be used without cost or restriction for educational purposes. There are sometimes conditions on using images, which should be consulted prior to use - for example, attribution. Many of these repositories licence their contents under <a href="http://creativecommons.org/">Creative
    Commons</a> as Open Educational Resources OER). The sole criterion in evaluating image sources below is: <em>are the images  cost-free, and usable without restriction, for educational purposes?</em></p>

<h2>Quick start</h2>
  <p>If you're in a hurry, try one of the following starting points:</p>
  <ul>
    <li><a href="http://www.everystockphoto.com/">Every stock photo</a>. A meta-search engine for free stock photography, indexing sources such as stock.xchng and Flickr.</li>
    <li><a href="http://www.openclipart.org/">Open Clip Art Library</a>. A large and growing collection of high-quality vector clipart (<abbr title="Scalable Vector Graphics">SVG</abbr> format) made
      available under the CC licence.</li>
  </ul>
