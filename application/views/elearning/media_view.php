<a name="dirs"></a>
<h2>Other directories and advice sites</h2>
<ul>
  <li><a href="http://en.wikipedia.org/wiki/Public_domain_image_resources">Wikipedia: Public domain image resources</a>. Probably <em>the</em> definitive list of PD image repositories, annotated
    and sorted by topic, plus a <a href="http://en.wikipedia.org/wiki/Public_domain_image_resources#General_collections">General
      Collections</a> category for mixed-topic image
    banks. Many of the repositories listed below have come from the Wikipedia list. </li>
  <li><a href="http://www.jiscdigitalmedia.ac.uk/">JISC Digital Media </a> (n&eacute;e <a href="http://www.tasi.ac.uk/advice/using/using.html">TASI</a> (Technical Advice Service for Images)) site which includes <a href="http://www.jiscdigitalmedia.ac.uk/stillimages/docs/category/finding-and-using-digital-media">detailed advice on finding usable images</a> and is a treasure trove of sound advice for UKHE users.</li>
  <li><a href="http://jiscmediahub.ac.uk/">JISC Media Hub</a>. A database of media collections, aimed primarily at UK Higher Education. The collections have various licences so read before you use. Much of the database is only available to subscribing institutions, but there is an <a href="http://jiscmediahub.ac.uk/about/unrestricted">unrestricted</a> section available to all. </li>
</ul>

