						<p>Selecting which sites to go into the collection has been <i>ad hoc</i> 
						from the start, although various loose selection criteria have evolved 
						as time progressed. In the early days of the collection, I actively searched 
						for language sites (of which there were precious few in 1995/6) to add. 
						As the collection has snowballed it has acquired its own momentum, and 
						now more time is spent evaluating sites submitted to me than in actively 
						seeking out new resources, although this does still take place.</p>
					  <h3>Selection criteria</h3>
					  <p align="left">There's no rigorous selection schema, but instead loose 
						criteria which have evolved over time. The main criterion for inclusion 
						is: </p>
					  <blockquote>
						<p><em>Does the site contain a significant amount of freely-available high-quality resources which would be 
						  of use to language teachers and/or learners in university education?</em></p>
					  </blockquote>
					  <p>This is because the target audience is primarily teachers and students 
						in the UK Higher Education sector.</p>
					  <p>Other loose evaluation criteria are:</p>
					  <ul>
						<li><em>Is the website usable and well-designed?</em></li>
						<li><em>Is the site usable by all browsers?</em></li>
						<li><em>Does the site contain resources which are not available elsewhere? 
						  </em></li>
					  </ul>
					  <p>And so on. As can be seen from the collection, though, some sites don't 
						necessarily fulfil all, or even any, of these criteria: they were added 
						on personal whim simply because it seemed like a good idea at the time 
						:)</p>
					  <h3><a name="commercial" id="commercial"></a>Commercial sites</h3>
					  <p>This
					is an amateur collection of links, not a commercial advertising site,
					and as it's only maintained in my spare time I'm increasingly picky as
					to what goes in it, as it takes significant time to evaluate sites. There are
					thousands of commercial languages sites, selling products and services.
					When, in the past, I included a Commercial section in the site, I was
					flooded with cut &amp; paste requests addressed to "Dear Webmaster",
					and spent increasing amounts of my time evaluating sites which offered
					little or no freely-available resources, such that the majority of link
					requests I was getting were from commercial sites seeking free
					advertising.</p>	
					<p>If you're a representative of a commercial site and the site only sells products/services, please do not email me requesting its inclusion in this list, as your email will be dropped into the 'memory hole'. If your site does have meaningful free resources, it will be considered for inclusion.</p>
					
