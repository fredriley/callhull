					<h2><a name="danish" id="danish"></a>Danish</h2>
					<p><a href="http://www.aok.dk/">Alt om K&oslash;benhavn - AOK</a>. A "What's on" 
					  and culture guide to Copenhagen, including a "virtual tour" of the city, info 
					  on its various tourist attractions, and links to places to stay, eat, and drink. 
					  Site text in Danish and English.</p>
					<p><a href="http://www.speakdanish.dk"> Danish lessons online</a>. A 10-day course in beginners
					  Danish for English speakers, including audio clips, grammar, a pronunciation
					  guide, common phrases, and useful links for learners of Danish. The audio clips
					  are implemented in Flash, so a Flash player is required.</p>
					<p><a href="http://www.danmark.dk/"> danmark.dk</a>. The "official door to Denmark" 
					  run by the Danish State Information Service, focussing on public institutions, 
					  tourism, local administration, and business. Site text in Danish, with one page 
					  of links in English. </p>
					<p><a href="http://www.dr.dk/">DR Online.</a> The website of the Danish national 
					  broadcaster, with audio and video clips. Website text in Danish, with some pages 
					  available also in English.</p>
					<p><a href="http://www.kulturnet.dk/"> Kulturnet</a>. A host of resources on all 
					  aspects of Danish culture, including news and an events calendar. Site text 
					  in Danish and English.</p><p><a href="http://mylanguages.org/learn_danish.php">Learn Danish</a>. This is a nice introduction to Danish, including audio clips of
					words and phrases. Part of the wider <a href="http://mylanguages.org/">mylanguages.org</a> site which has
					lessons, vocabulary and clips for a wide range of world language </p>
					<p><a href="http://www.litteraturnet.dk/"> LiteratureNet</a>. A gateway to Danish 
					  literature, with annotated and categorised links to literature resources, including 
					  book archives, literary periodicals, and author websites. Site text in English. </p>
					<p><a href="http://www.dpb.dpu.dk/infodok/links.html">Nyttige netadresser m.h.p.
					  fremmedsprogsp&aelig;dagogik.</a>  An annotated list of links maintained by Elsebeth Rise relating to speech technology,
					  and to the use of technology in language
					  teaching and research. Some of the links are specific to the Danish language.
					  Site text in Danish. </p>
