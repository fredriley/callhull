					<h2><a name="swedish" id="swedish"></a>Swedish</h2>
					<p><a href="http://www.aftonbladet.se/">Aftonbladet</a>. The Swedish daily newspaper 
					  online.</p>
					<p><a href="http://www.filter.ac.uk/database/insightrecord.php?id=41"> Beginner's
						Swedish</a>. A series of short, image-intensive lessons focusing on everyday
						situations, including audio clips (MP3) and exercises. </p>
					<p><a href="http://www.kultur.nu/">Kulturn&auml;t sverige.</a> A large, categorised 
					  collection of links to Net sites related to Swedish culture. Site text in Swedish 
					  and English. </p>
					<p><a href="http://folkets-lexikon.csc.kth.se/folkets/folkets.en.html">People's Dictionary</a>.
					A contributory English &#8660; Swedish dictionary, based on the Lexin
					dictionary but expanded by 'crowdsourcing'. By default, lookup returns
					bare terms, which you can expand to include a number of fields, such as
					usage, definition and pronunciation. </p>
					<p><a href="http://www.sr.se/">Radio Sweden.</a> RealAudio broadcasts in Swedish, 
							English, Russian, Latvian, and Estonian. Site text in Swedish, with some 
							English. </p>
						  <p><a href="http://www.itv.se/boreale/samieng.htm">The Sami people.</a> 
							General information about the Sami people of Sweden, written in English.      </p>
						  <p><a href="http://www.scb.se/">Statistics Sweden.</a> Official statistics 
							on Sweden's demography, economy, and society, from the national statistics 
							office. Site text in Swedish and in English. </p>
						  <p><a href="http://www.si.se/"> Svenska Institutet</a>. Website of the Swedish 
							Institute with a wealth of information about Sweden - country, people, 
							language, and culture - including printable fact sheets and information 
							about Swedish language courses and cultural exchanges. Site text in Swedish 
							and English. </p>
						  <p><a href="http://www-lexikon.nada.kth.se/skolverket/sve-eng.shtml">Svelsk-engelskt 
							lexikon.</a> Online Swedish-English dictionary, giving full word definitions, 
							including examples of usage and compounds. The site also holds Swedish-Croatian, 
							-Bosnian, -Finnish, and -Greek dictionaries. Site text in Swedish, but 
							there is an <a href="http://www-lexikon.nada.kth.se/skolverket/swe-eng.shtml">English 
							interface</a> to the dictionary. </p>
						  <p><a href="http://www.kidon.com/media-link/sweden.shtml">Swedish media.</a> 
							A categorised and seemingly comprehensive list of links to Swedish print 
							and broadcast media online, from the impressive <a href="http://www.kidon.com/media-link/">Kidon 
							Media Link</a>. </p>
						  <p><a href="http://www.virtualsweden.net/">Virtual Sweden.</a> A hierarchical 
							directory of Internet sites in Sweden. </p>
						  <p><a href="http://www.vskol.educ.goteborg.se/"> Den Virtuella Skolen</a>. 
							Distance learning courses in writing Swedish primarily aimed at Sixth 
							Form students in Sweden, including how to write narratives, analyse arguments, 
							write descriptions, and write about literature. Site text in Swedish.      </p>
