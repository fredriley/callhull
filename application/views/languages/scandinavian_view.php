					<h2><a name="icelandic" id="icelandic"></a>Icelandic</h2>
						  <p><a href="http://www.ismennt.is/">Icelandic Educational Network</a>. Lots of 
							useful educational resources in Iceland. In Icelandic, with versions of 
							the website in other Scandinavian languages and English. </p>
						  <p> <a href="http://jedi.cis.temple.edu/%7Efeeley/icetrans.html">Icelandic 
							phrase page.</a> How to pronounce Icelandic words and phrases, in text 
							and audio. </p>
						  <p><a href="http://www.simnet.is/gardarj/gardar1.htm">Virtually Virtual Iceland.</a> 
					  Tourist information on Iceland, including a few sound files of common Icelandic 
					  phrases. </p>
