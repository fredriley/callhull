					<h2><a name="general" id="general"></a>General</h2>
					<p><a href="http://www.ualberta.ca/%7Egerman/Jump_Stations.html">Jump Station to 
					  Resources in German and Scandinavian</a>. Pointers to German and Scandinavian 
					  resources on the Internet.</p>
					<p><a href="http://www.norden.org">Nordic Council of Ministers.</a> Facts, figures, 
					  maps, and news, about the Nordic region and individual countries. Website text 
					  in Swedish, Finnish, Icelandic, and English. </p>
					<p> <a href="http://www.markovits.com/nordic/">The Nordic Pages</a>. A comprehensive 
					  list of links to Internet sites in Scandinavia, together with general country 
					  information. An excellent starting point. (This site used to be known as <i>Nordic 
					  News and Media.</i> ) </p>
					<p><a href="http://www.lysator.liu.se/runeberg/"> Project Runeberg</a>. A growing 
					  collection of Nordic literary texts online.</p>
					<p><a href="http://www.byu.edu/sasslink/">SASSLink</a>. The Society for the Advancement 
					  of Scandinavian Studies. Information on the Society and pointers to Scandinavian 
					  resources on the Internet. </p>
						  <p> <a href="http://www.hull.ac.uk/Hull/SC_Web/wwwsites.htm">Scandinavian WWW 
							sites of interest</a>. A list maintained by Nick Gilmour of the Department 
							of <a href="http://www.hull.ac.uk/Hull/SC_Web/">Scandinavian Studies</a> 
							here at Hull University, including links to Scandinavian universities 
							and online media (newspapers, magazines, TV stations). </p>
