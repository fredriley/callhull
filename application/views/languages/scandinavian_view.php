					<h2><a name="finnish" id="finnish"></a>Finnish</h2>
						  <p><a href="http://www.eskimo.com/%7Ep34/project34/links.htm">Finland Related Links.</a> 
							A collection of links to Finland- and Finnish-related Internet resources, 
							sorted by type. </p>
						  <p><a href="http://www.uta.fi/%7Ekm56049/finnish/">Finnish Grammar Bits.</a> 
							A detailed, although incomplete, guide to Finnish grammar for the novice.      </p>
						  <p> </p>
						  <p><a href="http://virtual.finland.fi/finfo/english/finnlang.html">The Finnish 
							Language.</a> An introduction to the structure, history, and grammar of 
							Finnish. Although there are no lessons, this could be a useful primer 
							for prospective learners of the language. (Unrelated to the above site 
							of the same title.) </p>
						  <p><a href="http://call.lingnet.org/resource/language/finlr000.htm">Finnish 
							Language Learning Resources.</a> A short, annotated list of Internet resources 
							for learners of Finnish. </p>
						  <p><a href="http://www.helsinginsanomat.fi/">Helsingin Sanomat.</a> The 
							online edition of the Helsinki daily paper. There's also an <a href="http://www.helsinki-hs.net/">international 
							edition in</a> English. </p>
						  <p> <a href="http://eunuch.ddg.com/LIS/InfoDesignF97/paivir/finnish/sitemap.html">Introduction 
							to Finnish.</a> Lessons in basic Finnish with extensive grammatical notes, 
							plus links to resources on Finnish language and culture. </p>
						  <p><a href=" http://www.finnish-online.com">Learn Finnish Online</a>. A Moodle-based site for beginners to Finnish, which appears to be free to use. You have to send an email to be registered on the site and to access the learning materials.</p>							
						  <p> <a href="http://www.finlit.fi/kalevala/">SKS: Kalevala</a>. A site dedicated 
							to the Finnish national epic, The Kalevala. In Finnish, English, French, 
							and German. </p>
						  <p> <a href="http://fi.soneraplaza.net/">Sonera Plaza</a>. News, views, 
							features, and chat in Finnish. The site is run by the mobile phone company 
							Sonera. </p>
						  <p><a href="http://virtual.finland.fi/">Virtual Finland.</a> The official 
							site of the Finnish Foreign Ministry, dedicated to promoting Finnish culture 
							and language. Lots of useful information and links. </p>
						  <p> <a href="http://www.yle.fi/">Yleisradio / yletv.</a> The Finnish national 
							broadcasting company online, also in Swedish and <a href="http://www.yle.fi/fbc/">English</a> 
							(YLE International), with radio and TV broadcasts in Real Audio, and an 
							<a href="http://www.yle.fi/tv1ope/">educational section</a>. </p>
						  <p><a href="http://www.edu.fi/oppimateriaalit/ymmarrasuomea/">Ymm&auml;rr&auml; 
							suomea!</a> An online Finnish comprehension course, with a number of texts 
							each concentrating on a particular grammar point, plus an online (non-searchable) 
							Finnish =&gt; English dictionary and grammar reference. The site is completely 
							written in Finnish. </p>
