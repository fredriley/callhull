					 <p><a href="http://www.fixoodle.com/">Fixoodle</a>. Social tools for language learners. A site for language learners to connect with native and fluent speakers online, to get feedback, corrections and crowdsourced translations of passages. You have to register (free), specify your native and target language(s), then you can connect with other users.</p>
					 
