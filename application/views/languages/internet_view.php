					<p><a href="http://itilt.eu/">Interactive Technologies in Language Teaching (ITiLT)</a>. A repository of resources for language teachers and trainers using interactive whiteboards: training manuals, teaching resources and over 250 video examples of classroom practice. A European Union project. </p>
