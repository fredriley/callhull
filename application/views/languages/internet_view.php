					<h2><a id="software" name="software"></a>Software</h2>
					<p><em>Downloadable software which can be used to create
						browser-based exercises</em></p>
					<p><a href="http://hotpot.uvic.ca/">Half-Baked Software.</a>
					  The developers of the freeware Hot Potatoes suite, which
					  allows authors to create a range of interactive
					  browser-based exercises using Javascript without the need
					  for any prior HTML or Javascript knowledge. An excellent
					  piece of software, and highly recommended. The team have
					  also developed <a href="http://www.halfbakedsoftware.com/quandary.php">Quandary</a>,
					  with which you can create "Action Mazes". </p>
					<p><a href="http://www.spellmaster.com/">Spellmaster.</a> An
					  online authoring tool which enables you to create simple
					  browser-based timed vocabulary exercises (JigWord,
					  MatchWord, SpeedWord) which you can save to disk and use
					  on your students. You'll need the Flash 5 plugin as the
					  exercises are created as Macromedia Flash files. </p>
					<p><a href="http://ccat.sas.upenn.edu/plc/larrc/webauthor.html">Webauthor</a>.
					  Create interactive exercises by filling in forms on the
					  website. The exercises are stored as CGI scripts on the
					  Webauthor server. </p>
