					<h2><a id="general" name="general"></a>General</h2>

					<p><a href="http://flax.nzdl.org/">Flexible Language
						Acquisition Project (FLAX)</a>. A repository of
					  exercises for English teachers and learners, incorporating
					  social networking features (eg discussion, chat) for
					  peer-to-peer communication amongst students. The software
					  is Open Source, with an interface configurable to any
					  language (including non-Roman scripts). There's also a
					  FLAX Moodle plugin. It's currently available for for Linux
					  and Mac OS, with Windows to follow. <br />
					</p>
					<p><a href="http://www.vts.intute.ac.uk/he/tutorial/languages/">Internet
					for Modern Languages</a>. This tutorial covers the key
					  information skills for language teachers and researchers
					  using the Internet. Learn how to use the Internet to help
					  with your coursework, literature searching, teaching or
					  research. </p>
					<p><a href="http://www.ltss.bris.ac.uk/publications/guides/">LTSS
						Guides</a>. A series of guides to learning technology
					  produced by the Learning Technology Support Service at the
					  University of Bristol. Topics covered include implementing
					  and evaluating learning technology, computer-mediated
					  conferencing, virtual learning environments, and
					  teleconferencing for learning. Although slanted towards
					  users at Bristol, the guides are generic enough to be
					  useful for any teacher in higher education. </p>
					<p><a href="http://www.language-exchanges.org/">Mixxer:
						Language Exchange online via Skype</a>. A free exchange
					  bringing together language learners and teachers in tandem
					  learning via the VoIP technology <a href="http://www.skype.com">Skype</a>.
					</p>
					<p><a href="http://www.ex.ac.uk/pallas/relate/">ReLaTe</a>:
					  Remote Language Teaching using videoconferencing. A joint
					  project of the University of Exeter and University College
					  London, UK. </p>
					<p><a href="http://www3.unileon.es/personal/wwdfmrod/videoc.html">
						Reports on videoconferencing in the Language Classroom</a>.
					  Links to reports, websites and papers on the use of VC in
					  foreign language teaching.</p>
					<p><a href="http://www.sloan-c.org/">SLOAN-C</a>. A resource
					  centre for teachers interested in the use of
					  Internet-based teaching and learning. <br />
					</p>
					<p><a href="http://polyglot.lss.wisc.edu/lss/lang/teach.html">Teaching
						with the web</a>. Page with various ideas and pointers
					  for using the web as a teaching resource. </p>
					<p> <a href="http://www3.unileon.es/personal/wwdfmrod/collab/">
						Telecollaboration</a>. Resources for foreign language
					  teachers using internet communication tools for teaching
					  and intercultural exchanges.</p>
					<p><a href="http://www.mcli.dist.maricopa.edu/show/what/index.html">What
						a Site! Finding, Evaluating, and Integrating Web Sites.</a>
					  A sort of 'tutorial' for educators using the WWW as a
					  teaching resource, to help them meaningfully integrate
					  Internet materials into their teaching rather than just
					  providing students with a "laundry list" of hyperlinks. </p>
					<p></p>
