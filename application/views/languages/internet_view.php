					<h2><a id="blogging" name="blogging"></a>Blogging</h2>
					<p><em>Sites related to weblogging (or 'blogging' if you
						must) for both general and languages audiences.
						Suggestions for sites, articles and papers dealing
						specifically to weblogging and language
						learning/teaching would be very welcome.</em></p>
					<p><a href="http://www.elearnspace.org/Articles/blogging_part_1.htm">The
						Art of Blogging</a>. A good, considered introduction for
					  beginners to what the hell 'blogging' actually is and how
					  useful it can be, full of useful tips and references, and
					  links to weblogging resources such as tools and blog
					  servers. An excellent 'how to' starting point for the
					  weblog neophyte.</p>
					<p><a href="http://blog.lib.umn.edu/blogosphere/"> Into the
						Blogsphere</a>. A title that sounds like a crass 1950s
					  B-movie, but a site that contains serious analyses and
					  critiques of weblogs and weblog communities, including
					  studies of the pedagogical value of weblogs.</p>
					 <hr />
