					<h2><a id="vle" name="vle"></a>Virtual Learning Environments
					  (VLEs)</h2>
					<p><em>Also known as Managed Learning Environments (MLEs).</em></p>
					<p><a href="http://comentor.hud.ac.uk">coMentor</a>. A
					  collaborative web-based virtual learning environment. The
					  software runs on Unix servers, is accessible to all
					  Java-capable browsers, and is free to UK Higher Education
					  institutions. </p>
					<p><a href="http://www.staffs.ac.uk/COSE/">COSE Virtual
						Learning Environment.</a> An online learning
					  environment, developed at the University of Staffordshire,
					  UK, which is now available with a free licence (from
					  21/6/02), and which will shortly be Open Source. The site
					  contains the binary downloads, plus extensive support
					  documentation, case studies, reports, and a FAQ. COSE is
					  committed to interoperability via conformance with IMS
					  specifications. </p>
					<p><a href="http://moodle.org/"> Moodle</a>. An Open Source
					  VLE project, with strong language support (available in 34
					  languages) and based on social constructivist pedagogy.
					  The strongest competitor to proprietary commercial VLEs,
					  and in extensive use in education across the world.</p>
					  
