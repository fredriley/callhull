				<h3><a name="Learning" id="Learning"></a>Learning</h3><p><i>Online 
				lessons, exercises, and learning materials for students of English.</i></p>
				<p><a href="http://www.eviews.net/">Accents in English with eViews.</a> Weekly comprehension exercises based on recorded interviews (MP3, RealAudio) in different English accents, which subscribers can download 
				from the site. Subscription is charged to EFL teachers, but is free to students. 
				There's also a list of EFL-related Internet links, some of which are annotated.</p>
				<p><a href="http://www.rajeun.net/esl.html">The Amazing Cognate Poems Homepage</a>: Laugh and learn. A few French and English lessons based on funny poems.</p>
				<p><a href="http://www.eslcafe.com/">Dave's ESL 
				Cafe</a>. "Where learning English is Fun!". A friendly website for students and 
				teachers of ESL, with language resources and online chat rooms. </p><p><a href="http://english.wsl.edu.pl/"> 
				English@WSL</a>. A large number of interactive quizzes, sorted by level, for learners 
				of English, plus a database of annotated links for English learners, searchable 
				by category, and links to current news stories. The site has been created by students 
				at the <a href="http://www.wsl.edu.pl/">College of Foreign Languages</a> in Czestochowa, 
				Poland.</p><p><a href="http://mofetsrv.mofet.macam98.ac.il/%7Eelaine//eti/">English 
				through the Internet.</a> A 'virtual course' using the Internet to teach English 
				via distance learning.</p><p><a href="http://www.eslHouse.com/"> ESL House</a>. 
				Resources for students and teachers of English, including directories of schools 
				and services, an illustrated slang dictionary, short stories, and audio clips. 
				The site uses a house and room design metaphor.</p>
				<p><a href="http://www.english-at-home.com/">English@home</a>. 
				A large compendium of resources for people learning English at home, including 
				grammar and vocabulary, "Real lifeEnglish", a "Radio Room" 
				where you can listen to English radio stations online, online fora, and a free 
				email newsletter. </p>
					  <p> <a href="http://www.englishclub.net/">EnglishClub.net</a>. Lots of resources, 
						including reference materials, study aids, and quizzes, for teachers and 
						learners of English. </p>
					  <p><a href="http://www.study.com/">English School 
				on the net</a>. Free online English classes for beginners to advanced students.</p>
					  <p><a href="http://examspeak.com">ExamSpeak</a>.
				An interesting approach to preparing for Key English Test (KET) exams,
				using a gaming engine to create a Second Life-style virtual simulations
				of English tests so that the student can approach the experience of a
				real live interview. Sample questions and lessons are available for
				free, as is the speech comparison test - the full question bank, and
				live exam test, require a &#8364;20 subscription. The simulations require the
				installation of the Unity 3D gaming engine. </p>
					  <p><a href="http://fceblog.blogspot.com/">FCE Blog</a>.
				A weblog written, and continually updated, by Claudia Ceraso, a teacher
				of English in Argentina, which she describes as "a webguide to online
				practice for students preparing for the Cambridge FCE [First
				Certificate in English] exam. It offers guide and advice on all parts
				of the test as well as reflection on how students may become more
				autonomous learners. " </p>
				<p> <a href="http://www.freeenglish.com/">FreeEnglish</a>. Games for English learners 
				(developed in Flash) and downloadable EFL software for users who sign up to free 
				site registration.  </p>
				<p><a href="http://www.manythings.org/"> 
				Interesting Things for ESL Students</a>. Interactive activities using Flash, Javascript, 
				and Java, for students of English, including quizzes, sentence generators, and 
				Hangman. The focus is on American English. </p>
			  <p><a href="http://www.ucl.ac.uk/internet-grammar">Internet Grammar of English.</a> 
				An online course in English grammar written primarily for university undergraduates, 
				including Javascript exercises and a glossary of grammatical terms. </p>
			  <p> <a href="http://www.ruthvilmi.net/hut/Current/iwe.php">International 
				Writing Exchange (IWE)</a>. An email project, based at the University 
				of Helsinki, Finland, for students of English worldwide. </p>
				<p><a href="https://guthan.wordpress.com/">Island Voices / Guthan nan Eilean</a>. This publicly-funded bilingual project, coordinated by Sabhal M&ograve;r Ostaig, "aims to collect video slices of life and work in the Hebrides, primarily for language learners". This blog contains links to, and news about, video clips shot in the islands, including the "classic" Series 1 which are <a href="http://www.languages.dk/digital/indexGD.html">available for download</a> from the <a href="http://www.languages.dk/">POOLS website</a>, and for viewing on <a href="http://www.youtube.com/user/GordonWellsUist">Gordon Wells' YouTube channel </a> together with many other interesting videos in Gaelic. Highly recommended (and not just because Gordon was my first Gaelic teacher).&nbsp;</p>
					  <p><a href="http://www.britishcouncil.org/learnenglish">Learn 
				English.</a> The British Council Website for learners of English. The site has 
				a range of literacy materials, information about courses and links to other Websites. 
				Learners can enter the site using their first language and identify the learning 
				zone appropriate to their learning level. Languages currently available are Arabic, 
				Chinese, French, German, Italian, Portuguese, Russian and Spanish. </p>
					  <p><a href="http://www.learningchocolate.com/">Learning Chocolate</a>.
				Lots of cute interactive vocabulary learning resources, using pictures
				and audio clips implemented in Adobe Flash, and sorted into categories
				(eg animals, camping, body, etc). There's also a small collection of
				links to resources. </p>
				<p><a href="http://www.learning-english-online.net/">Learning English Online</a>. A "portal" for learners of English - beginners, travellers and business people. The site includes a <a href="http://www.learning-english-online.net/language-course/introduction/">course</a> for beginner and intermediate learners with text, quizzes, audio and video clips, plus resources such as vocabulary, grammar and pronunciation, and a section on skills (reading, listening, writing and speaking). There are also dialogues (audio) of standard situations (on the phone, in a restaurant, etc) and videos of famous speeches by politicians and public figures. Quite a chunky site with lots for learners to get their teeth into. </p>
			  <p><a href="http://www.bbc.co.uk/worldservice/learningenglish">Learning 
				English with the BBC.</a> Learn or improve your English with this interactive 
				multimedia website run by the BBC, which uses authentic audio and video 
				clips. Beginners lessons are available in an eclectic selection of source 
				languages, including Mandarin, Russian, and Ukrainian. </p>
			  <p><a href="http://www.mansioningles.com/">La Mansion del Ingles</a>. A 
				free multimedia beginners English course for Spanish speakers, comprising 
				12 lessons, interactive exercises, and useful additional resources (grammar, 
				vocabulary, song lyrics). The audio clips are in .wma format and require 
				the <a href="http://www.winamp.com/">Winamp player</a> (full version), 
				and unfortunately the site is currently only usable in Internet Explorer. 
				Site text in Spanish. </p>
			  <p><a href="http://www2.gol.com/users/norris/roadsdownload.html">The Many 
				Roads to Japan.</a> A downloadable interactive hypermedia book (PDF) by 
				Robert W Norris which tells the story of a Vietnam War war resister's 
				journey of self discovery. Aimed at ESL students and teachers (intermediate 
				and above levels), the book includes hyperlinks, essay and comprehension 
				questions, and exercises. The website also has a reading by the author 
				in Real format. </p>
					  <p><a href="http://www.esl-lab.com/">Randall's ESL 
				Cyber Listening Lab.</a> Lots of English listening quizzes using audio and video 
				materials. Each quiz has Pre-listening, Listening, and Post-Listening activities, 
				and the transcript of each clip is available with popup definitions of target 
				words. There's also a Bulletin Board and Chat Centre. </p>
					  <p><a href="http://www.kyoto-su.ac.jp/%7Etrobb/index.html">Tom 
				Robb's page.</a> Email projects of Japanese students learning English (in English 
				and Japanese), publications by Prof. Robb, and a few links. Hosted by the Dept 
				of English, Kyoto Sangyo University, Japan. </p><p><a href="http://www.stuff.co.uk/wicked.htm">Wicked 
				stuff for English learners</a>. Interactive multimedia activities, most of which 
				require browser plug-ins (Shockwave, RealAudio). </p>
