				  internationally recognised TEFL qualifications, such as the Cambridge CELTA
				  and Trinity CertTESOL. The site has detailed information on various EFL schools
				  worldwide teaching recognised qualifications, including each school's application
				  form. There's also advice on applying for courses, and articles from first-time
				  EFL teachers 'in the field', plus useful links for novice EFL teachers.</p>
				<p><a href="http://ESLworldwide.com/">ESLWorldwide.com</a>. 
				Advice and job listings for English teachers abroad. The site has bulletin boards 
				for jobs offered and wanted, and for English teachers to exchange ideas and experiences.</p><p><a href="http://www.jobsinjapan.com">EFL/ESL Jobs in Japan</a>. Listings 
				of currently available jobs for English teachers in Japan. </p><p><a href="http://www.eltnews.com">ELT 
				News</a>. A newsletter for ELT teachers in Japan. </p><p><a href="http://www.eslemployment.com/"> 
				ESL Employment</a>. An online 'labour exchange' for employers and TEFL teachers. 
				Teachers can register for free to view and receive job listings. There's also 
				an online teachers forum.</p><p><a href="http://www.eslmag.com/">ESL Magazine.</a> 
				A bi-monthly magazine for ESL professionals. </p><p><a href="http://www.iatefl.org/">IATEFL</a>: 
				International Association of Teachers of English as a Foreign Language. </p><p><a href="http://iteslj.org/"> 
				The Internet TESL Journal</a>. A monthly web journal for TEFL teachers, with articles, 
				lesson materials, and links. A section of the site - <a href="http://a4esl.org/">Activities 
				for ESL Students</a> - has activities for learners of English. </p>
				<p><a href="http://www.tea-austria.org/">Teachers of English in Austria.</a> 
						TEA is a professional organisation for TEFL teachers working in Austria. 
						As well as information about the organisation and its activities, the 
						site has links to useful TEFL-related sites. </p>
					  <p><a href="http://www.tealit.com/">Teaching English in Taiwan.</a> A survival 
						guide for English teachers who want to work in Taiwan. </p>
					  <p><a href="http://www.tefl.net">TEFL.net</a>. A resource centre for teachers 
						of English, including a Job Centre, discussion fora, and an online store 
						where you can buy teaching materials. </p>
					  <p><a href="http://www.tefl.com/">TEFL Professional Network.</a> A resource 
						for TEFL teachers, including an up-to-date list of jobs sorted by country 
						and links to TEFL resources on the Internet. </p>
					  <p><a href="http://www.zait.uni-bremen.de/wwwgast/tesl_ej/">TESL Electronic 
						Journal</a>. "TESL-EJ is a refereed electronic journal publishing articles 
						in the research and practice of the teaching of English as a second or 
						foreign language." </p>
					  <p><a href="http://www.tesol.org/">TESOL Online</a>. The website of Teachers 
						of English to Speakers of Other Languages, Inc. </p>
