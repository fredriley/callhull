				<h3><a name="dictionaries" id="dictionaries"></a>Dictionaries</h3><p><i>Online 
				dictionaries, grammar references, thesauri, and etymologies.</i></p><p><a href="http://dictionary.cambridge.org/">Cambridge 
				International Dictionaries Online.</a> Search online Cambridge dictionaries of 
				English, American English, Phrasal Verbs, and Idioms. Very useful. </p><p><a href="http://www.dict.org/">The 
				DICT Development Group</a>. A project to develop a protocol and tools to develop 
				and query online natural language databases. The site currently features an interface 
				to several online English dictionaries, allowing the user to query all or any 
				of them at one time using advanced search criteria.</p>
				<p><a href="http://www.dictionary.com">Dictionary.com</a>. 
				Online searchable English dictionary, including links to other English and non-English 
				online dictionaries and a glossary of English jargon terms. Learners can also create flash cards for vocabulary revision. </p>
				<p><a href="http://www.hyperdictionary.com/">Hyperdictionary</a> (n&eacute;e The 
				Exploding Dictionary). A sort of 'meta' search form for various publicly-available 
				English dictionaries (including Websters, the Hackers Dictionary, and the Jargon 
				File) which generates definitions of a term from these dictionaries in both plain 
				and hypertext format, so that you can follow links to related material. </p><p><a href="http://www.freedictionary.org/">FreeDictionary.org</a>. 
				A meta search engine, which searches one or more online English-language dictionaries 
				(eg Websters, Free Online Dictionary of Computing). You can search for words, 
				prefixes, substrings, or even Unix-style regular expressions. There's also an 
				anagram word game, although it's not entirely obvious how to play it. </p><p><a href="http://www.englandsnortheast.co.uk/GeordieDictionary.html">Geordie 
				Dictionary.</a> A dictionary, on one page, of one of the most impenetrable regional 
				dialects in the UK. </p>
				<p><a href="http://www.bennetyee.org/http_webster.cgi">Hypertext 
				Webster Gateway </a> A searchable online version of the Webster English Dictionary.</p><p><a href="http://people.kldp.org/%7Eeunjea/jargon/">The Jargon Dictionary.</a> A 
				searchable and browsable dictionary of English jargon and slang terms, with particular 
				reference to hacker slang (eg troll, hacker, phreaker). </p><p><a href="http://www.kplay.cc/reference.html"> 
				KnowPlay Reference</a>. A 'meta search engine' which allows you to search various 
				dictionaries, thesauri, and encyclopedia from a single form.</p><p><a href="http://www.edufind.com/english/grammar/">Online 
				English Grammar.</a> A comprehensive reference hosted by St. John's Wood School 
				of English, London. </p><p><a href="http://www.oxfordreference.com/views/SUBJECT_SEARCH.html?subject=s7"> 
				Oxford Reference: English Dictionaries and Thesauruses</a>. Online, searchable 
				versions of Oxford English dictionaries and thesauri, giving full definitions 
				and etymologies, with a useful cross-reference feature to follow leads. </p><p><a href="http://humanities.uchicago.edu/forms_unrest/ROGET.html">Roget's 
				Thesaurus.</a> An online search form for the famous English thesaurus. </p>
				<p><a href="http://www.peevish.co.uk/slang/links.htm">Slang Dictionaries and Language Links</a>.
				A categorised and annotated collection of links to slang dictionaries
				worlwide, though concentrating almost exclusively on the
				English-speaking world.</p><p><a href="http://www.pdfconverter.com/resources/blog/top-business-dictionaries-online">20 Essential Business Dictionaries online</a>.
				An annotated list of links to dictionaries helpful to anyone trying to
				get their head around the often baffling - even to native English
				speakers! - dialect of English as spoken in business.  </p>
				<p><a href="http://en.wiktionary.org">Wiktionary</a>. An open contributory multlingual dictionary, by the folk who brought you Wikipedia. Searchable by term, browsable by language.</p>
				<p> <a href="http://www.lightlink.com/bobp/wedt/wedt-artfl.html">Wordsmythe 
						English Dictionary-Thesaurus</a>. An online English dictionary with a 
						difference, as the search produces related words and phrases as well as 
						definitions. </p>
					  <p> <a href="http://www.worldwidewords.org/">World Wide Words: exploring 
						the English language</a>. A fascinating and highly browseable site containing 
						the derivation and history of hundreds of English words and phrases, including 
						very recent terms. Indexed and searchable. Recommended. </p>
