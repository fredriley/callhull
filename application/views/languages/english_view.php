				  SAT preparation words</a>. An alphabetical list of 5000 English words with brief 
				  definitions.</p>
				<p><a href="http://www.baysights.com/aardvark/">Aardvark's EFL 
				Resources.</a> Links, resources and contacts for students and teachers of EFL. 
				There are Interactive Exercises practising grammar, vocabulary and idioms at mixed 
				levels, and a Flags &amp; Countries Quiz.</p>
				<p><a href="http://bogglesworldesl.com/"> 
				Boggle's World: ESL activities for adults</a>. Stacks of free printable (MS Word format) 
				teaching materials and activities for teachers of ESL to adults. The site also has a site directory for ESL teachers. </p>
				<p><a href="http://education.guardian.co.uk/tefl/">Education 
				Guardian: TEFL.</a> Hosted on the website of The Guardian, this site has lots 
				of resources for EFL teachers and students, including a directory of annotated 
				links, lesson plans based on news stories, and a teacher's forum. </p><p><a href="http://www.eltcalendar.com/"> 
				</a><a href="http://membres.lycos.fr/jcviel/">EFL Tech - France</a>. A website 
