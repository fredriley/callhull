					  <h3><a name="chinese" id="chinese"></a>Chinese </h3>
					  <p><a href="http://www.bbc.co.uk/mandarin/">BBC Mandarin Online</a>. News 
						stories and RealAudio broadcasts in Mandarin from the <a href="http://www.bbc.co.uk/worldservice/">BBC 
						World Service</a>. Also available in <a href="http://www.bbc.co.uk/cantonese">Cantonese</a>. 
					  </p>
					  <p><a href="http://www.bridgetochina.org.uk/">Bridge to China</a>. A wiki hosted at the University of Central London, with Mandarin Grammar and Resources on China. </p>
					  <p><a href="http://www.ctcfl.ox.ac.uk/">Centre for Teaching Chinese as a 
						Foreign Language.</a> The CTCFL site, hosted at the University of Oxford, 
						has a large number of annotated and categorised resources for learners 
						of Chinese at all levels. They've recently developed an online elementary 
						Chinese course, for which you need to apply for a login name and password. 
					  </p>
					  <p> <a href="http://www.webcom.com/~bamboo/chinese/chinese.html">Chinese-Language-Related 
						Information page.</a> </p>
					  <p> <a 	href="http://heather.cs.ucdavis.edu/~matloff/chinese.html">Chinese 
						Software Web Site</a>. The main feature of this website is the downloadable 
						KuaiXue software for learning Chinese. </p>
					  <p> <a href="http://www.chinapages.com/">China Pages</a>. Official news 
						and information about the PRC, mostly in English, but with some pages 
						in Chinese. However, there's no indication on the site as to which font 
						to use to read the Chinese. </p>
					  <p> <a href="http://exodus.lcsc.edu/cbiouser/introduc.htm"> The Chinese 
						Biographical Database</a>. A moderated online database useful for research 
						and as an educational tool. </p>
					  <p> <a href="http://www.erols.com/eepeter/chardict.html">Chinese Character 
						Dictionary.</a> Interactive English/Pinyin/Cantonese dictionary. </p>
					  <p><a href="http://www.chinese-outpost.com/"> The Chinese Outpost</a>.
						Subtitled &quot;Demystifying
						Chinese Since 1998&quot; this site contains a wealth of resources on
					  spoken and written Chinese, including a tutorial in Mandarin. </p>
					  <p><a href="http://www.csulb.edu/~txie/ccol/content.htm">Learning Chinese 
						Online</a>. Links to online material or the learner of Chinese, including 
						text and audio lessons and online dictionaries. </p>
					  <p><a href="http://www.hku.hk/curric/chincal/dragonw.html">Multi-media Project 
						for the Teaching and Learning of Chinese</a> at the University of Hong 
						Kong. This project is developing CAL software for learning Chinese, and 
						a shareware version is available for download from the site. </p>
					  <p><a href="http://www.ocrat.com/ocrat/">Ocrat Chinese Pages.</a> Learn 
						to draw Chinese characters by watching animated GIFs, and study intermediate-level 
						Mandarin Chinese by listening to <a href="http://www.voa.gov/voahome/">VOA 
						broadcasts</a>. </p>
					  <p> <a href="http://www.erols.com/eepeter/chtools.html">Online Chinese Tool</a>s. 
						Resources to assist learners and users of Chinese, including interactive 
						Chinese character flashcards (uses Java), a character dictionary, a module 
						to add pinyin to files, and encoding converters. </p>
					  <p><a href="http://www.snweb.com.cn/">The People's Daily of China</a>. Online 
						version of the official Chinese daily paper. Site text in Chinese. There 
						are also editions of the paper in <a href="http://english.peopledaily.com.cn/">English</a> 
						(<a href="http://www.peopledaily.net/english/">US mirror</a>), <a href="http://french.people.com.cn">French</a>, 
						<a href="http://spanish.people.com.cn">Spanish</a>, <a href="http://russian.people.com.cn">Russian</a>, 
						and <a href="http://arabic.people.com.cn">Arabic</a>. </p>
					  <p><a href="http://www.webcom.com/ocrat/voa/">Study Mandarin using VOA</a>. 
						Intermediate-level Mandarin Chinese lessons using Voice of America sound 
						files (RealAudio format) and Chinese transcripts. </p>
					  <p><a href="http://zhongwen.com/">Zhongwen Zipu</a>: Chinese Character Genealogy. 
						An "etymological Chinese-English Dictionary". </p>
