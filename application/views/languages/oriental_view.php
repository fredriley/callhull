					  <h3><a name="korean" id="korean"></a>Korean</h3>
					  <p><a href="http://www.koreantutor.com"> KoreanTutor.com</a>. Online Korean 
						language learning ranging from basic principles of the Korean alphabet 
						(Hangeul) and sentence patterns to high-level dialogues. All educational 
						content has been provided, and is continually added to, by Seoul National 
						University's Korean Language Education Research Institute. Also offers 
						additional fun and interesting methods to learn Korean, such as daily 
						tips on Korean, Korean pop songs and Theatre. In English, Japanese (and 
						soon in Chinese).</p>
					  <p><a href="http://www.geocities.com/mlovmo/gallery15.html">Language 
						Purism in Korea.</a> Paper by Mark S. Lovmo describing the history and 
						scope of the efforts of language purism in both North and South Korea.</p>
