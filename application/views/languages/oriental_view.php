					  <h3><a name="japanese" id="japanese"></a>Japanese </h3>
					  <p><a href="http://www.jlgweb.org.uk/btj/">Bridge to Japan</a>. A large, categorised directory of Japan-related Internet resources, founded in 1995 (and thus as old as CALL@Hull!). Link text in both English and Japanese.</p>
					  <p> <a href="http://www.notredame.ac.jp/cgi-bin/jedi-inon">Eva Japanese-English 
						dictionary</a>. A simple search engine allowing you to look up English 
						and Japanese terms. </p>
					  <p><a href="http://cwis.livjm.ac.uk/lan/teaching/japanese">Japanese at Liverpool 
						John Moores University.</a> Sight showing students activities, videos 
						of students in Japan doing karaoke, provides news and job/interview advice 
						from past students. </p>
					  <p> <a href="http://thejapanfaq.cjb.net">The Japan FAQ: know before you 
						go</a>. A guide to living and working in Japan maintained by Robert Murphy 
						of Seinan University. </p>
					  <p><a href="http://www.japanpoem.com/">Japan Poem.</a> A curious site which 
						allows you to create your own multimedia electronic 'greetings cards' 
						based around Japanese Imperial Court poetry. The poems are in Kanji, phonetic 
						transcription, and English. </p>
					  <p> <a href="http://www.japantraveler.com/">Japan Traveler</a>. Web version 
						of the English-language monthly about living and working in Japan. There's 
						also a Classified Ads section on the website. </p>
					  <p><a href="http://www.gol.com/jguide/">The Japan Web Guide.</a> Links to 
						WWW servers in Japan, sorted by category. Written in English. </p>
					  <p> <a href="http://linear.mv.com/cgi-bin/j-e/dict">Jeffrey's Japanese-English 
						dictionary</a>. You can search using English words and kanji characters, 
						and there's also a database on kanji characters. There's a <a href="http://enterprise.ic.gc.ca/cgi-bin/j-e/dict">mirror 
						site</a> in Canada. </p>
					  <p> <a href="http://www.rdt.monash.edu.au/~jwb/japanese.html">Jim Breen's 
						Japanese Page</a> at Monash University in Australia. Information on projects 
						at Monash, and links to other Japanese-related sites. </p>
					  <p> <a href="http://web.mit.edu/jpnet/index.html">JP Net: Japanese Language 
						and Culture Network</a>. A resource for Japanese language teachers and 
						learners worldwide, including a useful section on displaying Japanese 
						text in web browsers. Text in English. </p>
					  <p> <a href="http://www2.gol.com/users/billp/students/kanjiname/">Kanji 
						Names Project</a>. Japanese students explain the meanings behind the kanji 
						characters of their names. There are also some links to Kanji-related 
						sites. </p>
					  <p> <a href="http://www.kagawa-jc.ac.jp/~steve_mc/presence.html">Net Presence 
						of Steve McCarty</a>. The home page of a "Japanologist/EFL Professor" 
						at Kagawa Junior College, which has some useful links to Japanese language 
						projects. </p>
					  <p> <a href="http://www.nihongocentre.org.uk/">Nihongo Centre London</a>. 
						A resource site for teachers of Japanese in the UK, including news, events 
						diary, online forum, and job vacancies. </p>
					  <p> <a href="http://www.kyoto-su.ac.jp/~trobb/index.html">Tom Robb's page.</a> 
						Email projects of Japanese students learning English (in English and Japanese), 
						publications by Prof. Robb, and a few links. Hosted by the Dept of English, 
						Kyoto Sangyo University, Japan. </p>
					  <p> <a href="http://www.essex.ac.uk/centres/japan/Japanese/resources.html">Web 
						Resources for Teaching and Learning of Japanese Language</a>. A large 
						collection of links for teachers and learners of Japanese, sorted by category. 
					  </p>
