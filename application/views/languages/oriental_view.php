				<h3><a name="general" id="general"></a>General</h3>
					  <p><a href="http://www.cjvlang.com/">Chinese, Japanese and Vietnamese.</a> 
						Language learning site featuring sections such as Days of the Week, Glossary 
						of Bird names, Harry Potter in Chinese, Japanese, and Vietnamese. Also 
						includes a description of the Chinese, Japanese, and Vietnamese writing 
						systems and the Transliteration of Chinese, Japanese, and Vietnamese.</p>
					  <p><a href="http://dsal.uchicago.edu/dictionaries/">Digital Dictionaries 
						of South Asia.</a> Project to create and disseminate electronic dictionaries 
						for each of the twenty-six modern literary languages of South Asia. At 
						least one multilingual dictionary is being selected for each South Asian 
						language; in the case of more frequently taught languages, a monolingual 
						dictionary is also chosen. The dictionaries are available by means of 
						standard file transfer protocol, or by compact disc. There is no charge 
						for access via the Internet and the compact discs are available for the 
						cost of duplication and mailing.</p>
					  <p><a href="http://lark.cc.ukans.edu/%7Eeastasia/link.html">East Asian Studies 
						Internet Resources</a>. A large collection of links, sorted by type and 
						language, for students of East Asian cultures and languages, focussing 
						in particular on China, Taiwan, Japan, and Korea. </p>
					  <p><a href="http://www.seasite.niu.edu/">SEAsite.</a> The site describes 
						itself as "Interactive learning resources for SE Asian languages and cultures", 
						and behind the modest home page lies a wealth of learning resources, including 
						language lessons, dictionaries, grammars, and information on countries 
						and cultures. The site is divided into autonomous sections on <a href="http://www.seasite.niu.edu/Indonesian">Indonesia</a>, 
						<a href="http://www.seasite.niu.edu/Thai/default.htm">Thailand</a>, <a href="http://www.seasite.niu.edu/Tagalog">Philippines</a> 
						(Tagalog), <a href="http://www.seasite.niu.edu/vietnamese/VNMainpage/vietsite/vietsite.htm">Vietnam</a>, 
						<a href="http://www.seasite.niu.edu/Burmese">Myanmar</a> (Burmese), and 
						<a href="http://www.seasite.niu.edu/lao/">Laos</a>. The site is maintained 
						by the <a href="http://www.niu.edu/acad/cseas/">Centre for Southeast Asian 
						Studies</a> at the University of Illinois, USA. Recommended.</p>
