						version of the Italian newspaper, including all the sections and supplements 
						in the printed editions. </p>
					  <p><a href="http://www.diario.it/">Il Diario</a>. The Italian current affairs 
						weekly online. </p>
					  <p><a href="http://espresso.repubblica.it/">L'Espresso.</a> Online edition 
