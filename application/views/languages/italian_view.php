				<h3><a name="general" id="general"></a>General</h3>
					  <p><a href="http://www.amblondra.esteri.it/">The Embassy of Italy in London.</a> 
						Official website of the Italian Embassy, packed full of categorised resources 
						and information on Italy and all things Italian, behind a deceptively 
						bland home page. Recommended. </p>
					  <p><a href="http://www.ucl.ac.uk/Resources/Arts/italian.shtml">Internet Resources: 
						Italian</a>. A categorised list of links to Italian resources on the Internet.      </p>
					  <p><a href="http://www.istat.it/">Istituto Nazionale di Statistic (Istat) 
						</a>. Official statistics on Italy's demography, economy, and society, 
						from the national statistics office. A useful report is the annual "Italia 
						in Cifre", which gives a detailed overview of Italian economy and society, 
						including trends. Site text in English and Italian. </p>
					  <p><a href="http://www.italiantext.com/converter/">Italian accent converter</a>. This is a handy little online utility, which takes text written using apostrophes for accents, as is often written by users of English language keyboards, and converts it into the correct format. So the phrase &quot;E' un po' come le citta' italiane&quot; becomes &quot;&Egrave; un po' come le citt&agrave; italiane&quot;. You can also download a free (for personal use) Windows utility with extra functionality. </p>
					  <p><a href="http://www.italica.rai.it/">Italica.</a> A rapidly growing and 
						impressive website run by <a href="http://www.raiinternational.rai.it/">RAI 
						International</a>, which aims to be a "Virtual Campus of Italian language 
						and culture" on the WWW. A major feature of Italica is that it makes use 
						of the vast documentary heritage, both audio and video, in the RAI archives. 
						Recommended. </p>
					  <p><a href="http://info.uibk.ac.at/c/c6/it/tutt.html">TuttItalia</a>. "Il 
						sito dedicato all'italianistica." A collection of annotated links to Italian 
					  resources, including Language, Mass Media, and Society. </p>
					  <hr />
					   <h3><a name="current" id="current"></a>Current Affairs and Politics</h3><p><a href="http://clarence.dada.net/contents/societa/memoria/">Banca 
				Dati della Memoria.</a> An archive of official documents of recent and ongoing 
				judicial and parliamentary investigations, including the Andreotti trial, and 
				the inquiries into the Ustica and Piazza Fontana tragedies. </p>
					   <p><a href="http://www.sofri.org/">Il 
				Caso Sofri.</a> A site dedicated to the campaign to free Adriano Sofri, and his 
				comrades Bompressi and Pietrostefani, jailed for the murder of the police chief 
				Calabresi in 1972. In Italian, English, German, French, and even Serbo-Croat. 
				A potentially useful site for a course in Italian current affairs. The Italian 
				daily newspaper La Repubblica also has a <a href="http://www.repubblica.it/online/cronaca/sofri/sentenza/sentenza.html">section</a> 
				dedicated to the Sofri case. </p><p><a href="http://www.citinv.it/">La Citt&agrave; Invisibile</a>. 
				News and views from the Italian Left. </p><p><a href="http://www.fionline.it/mafie/">Cosa 
				Nostra Home Page.</a> Information (in Italian) on the various Mafia organisations 
				in Italy and the world. </p>
				<p><a href="http://www.forza-italia.it/">Forza 
				Italia.</a> The official website of the main right-wing party in Italy. </p><p><a href="http://www.partiti.net/">Italian 
				political parties</a>. Information on the main Italian political parties and their 
				programmes. </p><p><a href="http://www.misteriditalia.it/"> Misteri d'Italia</a>. 
				A journalistic-historical site dedicated to the countless unsolved 'affairs' over 
				the last half-century, such as Piazza Fontana, Ustica, the killing of Carlo Giuliani, 
				to name but a few. You can sign up for a regularly and detailed email newsletter 
				with updates on current stories. Produced by journalists using sound sources and 
				continuously updated, this site is a gold mine for anyone interested in post-war 
				Italian history and in current affairs. Highly recommended. </p><p><a href="http://www.verdi.it/">I 
				Verdi</a>. The website of the Italian Green Federation. </p>
