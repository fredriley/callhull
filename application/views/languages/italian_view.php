					   <h3><a name="government" id="government"></a>Government</h3><p><a href="http://www.governo.it/">Governo 
				italiano.</a> The official website of the Italian government. Lots of useful information 
				about ministeries and personnel, including biographies of ministers. </p><p><a href="http://www.esteri.it/">Ministero 
				degli Affari Esteri in Italia.</a> The official site of the Italian Foreign Ministry, 
				with current and past official documents (eg press releases, policy statements) 
				online, and information on the Ministry and its activities (including details 
				of study bursaries). A nicely-designed website which is easy on the eye, and which 
				also has English and text-only versions. </p>
					  <p><a href="http://www.camera.it/">Parlamento italiano</a>. The Italian 
						Parliament online, with extensive and detailed information on the activities, 
						composition, and procedures of both the Camera and the Senate. You can 
						also listen live to debates, or recordings of recent debates, via RealAudio. 
						Recommended. </p>
					  <p><a href="http://www.palazzochigi.it/">Palazzo Chigi.</a> The official 
						website of the Council of Ministers. </p>
					  <p><a href="http://www.senato.it/">Senato della Repubblica</a>. Website 
						of the Italian Senate, with information on its workings, laws and bills 
						in progress, composition, and much else. </p>
