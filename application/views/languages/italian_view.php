					  <p><a href="http://www.ilfoglio.it">Il Foglio</a>. Online newspaper in Adobe 
						<a href="http://www.adobe.com/products/acrobat/readermain.html">Acrobat</a> 
						format (PDF). </p>
					  <p><a href="http://www.gazzetta.it/">Gazzetta dello Sport Online.</a> The 
						Italian daily for the sport-obsessed online, including RealAudio clips. 
						There are also some very useful vocabularies for individual sports - choose 
						a sport, then click on "vocabulario". </p>
					  <p><a href="http://www.internazionale.it/">Internazionale.</a> Review of 
						the world's press, in Italian. </p>
					  <p><a href="http://www.liberazione.it/">Liberazione.</a> The newspaper of 
						the <a href="http://www.rifondazione.it/">Partito della Rifondazione Comunista</a>. 
						For light relief, there's also <a href="http://www.geocities.com/CapitolHill/1081/libnet.html"> 
						LiberazioNet</a>, a satirical weekly run by Prc members. </p>
					  <p><a href="http://www.ilmanifesto.it/">Il Manifesto</a>. Online version 
						of the independent Communist newspaper. The site also has Italian translations 
						of the left-wing French weekly <a href="http://www.monde-diplomatique.it/">Le 
						Monde Diplomatique</a>. </p>
					  <p><a href="http://www.panorama.it/">Panorama Online</a>. News 
						and articles from the current affairs magazine. There's also a "sputa 
						il rospo" section, where readers write in with stories they want investigating.      </p>
					  <p><a href="http://www.repubblica.it/">La Repubblica</a>. The Italian daily 
						newspaper online, which seems to feature all the stories that appear in 
						the paper version, and has complete back issues to the beginning of the 
						month and a search engine. A well-designed site with plenty of content. 
						Recommended. </p>
					  <p><a href="http://tv.repubblica.it/home">Repubblica TV e Radio</a>. Audio and video reports from the Italian centre-left newspaper <a href="http://www.repubblica.it/">La Repubblica</a>. You can subscribe to daily audio and video podcasts on your media player for offline consumption. A very handy resource for teachers and learners with authentic language on current issues.</p>
					  <p><a href="http://www.ilsole24ore.com/">Il Sole 24 Ore</a>. Online up-to-the-minute 
						business news in Italian. </p>
					  <p><a href="http://www.lastampa.it">La Stampa</a>. The 
				Italian newspaper online. As well as the online stories in HTML, there's a condensed 
				4-page PDF version of the newspaper called Dayfax, which is sadly unprintable.</p>
					  <p><a href="http://www.unionesarda.it/">L'Unione Sarda</a>. The Sardinian 
						newspaper online. </p>
					  <p> <a href="http://www.unita.it">Unit&agrave; in Rete</a>. The Italian 
						Communist newspaper on the Net. </p>
