					   <h3><a name="learning" id="learning"></a>Learning and Teaching</h3><p><i>Online 
				lessons and courses, grammar references, teaching materials, etc</i></p>

				
				<p> <a href="http://www.guerra-edizioni.com/">Guerra Edizioni.</a> Material 
						for teachers and learners of Italian, including an online forum to discuss 
						problems with the language, a chat room, activities and exercises, and 
						lots of links to Italian sites. </p>
					  <p><a href="http://www.italicon.it">Icon: Italian culture on the Net.</a> The
						Icon Consortium is composed of some 24 Italian universities, in collaboration
						with the ministries of Foreign Affairs and of Education, with the purpose
						of providing online courses and materials for foreigners and Italians
						resident abroad who are interested in Italian language, culture and history.
						The site is a 'portal' to a wide range of resources and services, and
						offers the opportunity to take a 3-year certificated degree course in
						Italian Language and Culture for Foreigners. You have to pay for many
						of the courses and resources, but there are also a good number of free
						courses and resources available for which you have to register (free
						of charge). A very interesting initiative worth keeping an eye on. Recommended.
					  Site text in Italian, English, Spanish, German, and French.</p>
					  <p><a href="http://www.iluss.it/">ILUSS - Italiano online.</a> Interactive 
						exercises, reading materials, and links for students and teachers of Italian. 
						Although this is a paid membership site, a significant amount of its resources 
						are freely-available to non-members. </p>
					  
					  <p><a href="http://www.locuta.com/classroom.html">The Italian Electronic 
						Classroom.</a> A grand title for basic, but very useful, tutorials and 
						exercises on "difficult" aspects of Italian language, such as idiomatic 
						expressions, pronunciation, prepositions, and pronouns. </p>
						<p><a href="https://sites.google.com/site/kittysitaliancrosswords/">Kitty's Italian crosswords</a>. Printable PDF crosswords for learners of Italian, focussing on particular language points (eg irregular passato prossimo, numbers), created by a mother teaching her daughter Italian. The crosswords are free to download and use.</p>
						<p><a href="http://www.intese.polimi.it/web/guest/italian_idioms">Learn Italian Idioms</a>. A blog serving an Italian idiom a day to learners of the language. Idiom and explanation in Italian, site text in English. </p>
					  <p><a href="www.scudit.net/matdid.htm">Materiali didattici 
						  per l'insegnamento dell'italiano</a>. Texts and exercises (non-interactive),
						  graded by level, for learners of Italian as a foreign language, using
						  continuously updated material. The exercises are useful resources for
						  teachers of Italian at all levels. </p>
					  <p><a href="http://academic.brooklyn.cuny.edu/modlang/carasi/site/">Oggi 
						e Domani.</a> An interactive 20-lesson course in Italian, ranging from 
						beginner to intermediate level. Each lesson includes exercises, audio 
						clips, and external links, and reference material (dictionary, grammar, 
						pronunciation) is always available. </p>
					  <p><a href="http://www.oneworlditaliano.com/">OneWorld Italiano</a>. Lots of text and multimedia exercises in Italian: grammar, comprehension, vocabulary and more. There are also YouTube videos and full lessons with audio. The site belongs to an Italian language school in Sardinia. Site text in Italian, English, French, Japanese and German. </p>
					  <p><a href="http://www.linguaitalia.com/archivio/talis/talis.html">Progetto 
						Talis.</a> An experimental project to put Italian texts and exercises 
						online for learners of Italian. </p>
					  <p><a href="http://www.univ.trieste.it/%7Enirital/texel/coni/conihome.htm"> Trieste
						  Verb Conjugator</a>. A useful Italian verb conjugator hosted at the University
						  of Trieste. Enter any verb for conjugation in any tense in active,
						  passive, or reflexive forms. The no-frills interface is a refreshing
					  relief from the eye-mangling designs of so many language sites.</p>
					  <hr />
					   <h3><a name="literature" id="literature"></a>Literature</h3><p><a href="http://www.liberliber.it/biblioteca/">La 
				Biblioteca Telematica</a>. A large collection of texts, including non-Italian 
				literature translated into Italian, sorted alphabetically by author. There's also 
				an experimental VRML library. </p>
					  <p><a href="http://www.italialibri.net/">La Libreria di Dora.</a> A site dedicated 
				to Italian authors and literature, ancient and contemporary, with biographies 
				of authors and critical analyses of their works. The site also has an online critical 
				magazine, and a facility for readers to add their comments to pages. Site text 
				mainly in Italian, with some English. </p><p><a href="http://www.ilnarratore.com">Il 
				Narratore</a>. A cultural non-profit organisation which aims to provide spoken 
				narrations of literary works from all over the world. Currently the site has written 
				works of many Italian authors, past and present, with audio samples (MP3) online. 
				Site text in English and Italian. </p>
