					   <h3><a name="dicts" id="dicts"></a>Dictionaries</h3><p><a href="http://www.dellamoda.it/">Dizionario 
				della Moda.</a> An encyclopedia of fashion, rather than a dictionary, with long 
				entries on topics such as individual designers, movements, and fashion terms. 
				</p><p><a href="http://www.garzantilinguistica.it/">Garzanti Linguistica</a>. 
				A very good online Italian-language dictionary. Type in a term to get a full dictionary 
				definition, including use of the term in idioms and expressions, and hyperlinks 
				to related terms. There's also an Italian-English dictionary on the site, which 
				includes audio files in MP3 format, but you have to register (for free) to use 
				it. Recommended. </p><p><a href="http://www.traduzioni-inglese.it/dictionary-glossary.html">Italian-English 
				Dictionaries.</a> Links to online dictionaries of use to Italian-English translation 
				- monolingual, bilingual, and multilingual. Includes some very useful and hard-to-find 
				dictionaries (eg Information Technology, proverbs, Medicine). </p>
				<p><a href="http://language.engagedthinking.com/">Online Italian-English dictionary</a>. Actually, it's rather more than a dictionary, as the site also has resources for learners, plus links to a blog and word of the day. The dictionary is a little idiosyncratic, in that a translation of, say, &quot;ring&quot; will only return the noun &quot;anello&quot; unless you either tell it to look for &quot;to ring&quot;, in which case you get the verb, or to look for &quot;ring&quot; as a substring, in which case you get every work with &quot;ring&quot; in, including &quot;fluttering&quot;, &quot;gathering&quot;, etc. </p>
