				<a id="dictionaries" name="dictionaries"></a>
				<h3>Online Dictionaries and Grammars</h3>
				<p><b>NB: </b><i>This category contains links to
					multi-lingual dictionaries/grammars and lists of
					dictionaries/grammars. Those dedicated to a particular
					language (eg French) or language pair (eg
					French-English) can be found in the appropriate language
					category.</i> </p>
				<p> <a href="http://alternative-dictionaries.net/">Alternative Dictionaries</a> Words you wouldn't usually find in
				  dictionaries (slang, idioms, swear words) in a wide range
				  of languages. You can contribute your own entries to a
				  dictionary. </p>
				<p><a href="http://en.bab.la/">bab.la Language Portal</a>.
				  This site has a sophisticated online dictionary with a
				  good number of language pairs, including Arabic, Chinese
				  and Hindi as well as the 'usual suspects'. Results are
				  detailed, including usage examples, sound clips, synonyms
				  and hyperlinks to related terms. The site also has:</p>
				<ul>
				  <li>interactive <a href="http://en.bab.la/quiz/">quizzes</a>
					and <a href="http://en.bab.la/games/">games</a> for
					learners</li>
				  <li><a href="http://en.bab.la/conjugation/">verb
					  conjugators</a></li>
				  <li><a href="http://en.bab.la/tools-plugins.php">tools and
					  plugins</a> (apps, browser plugins, etc)</li>
				  <li>learner <a href="http://en.bab.la/forum/">discussion
					  fora</a></li>
				  <li><a href="http://en.bab.la/phrases/">phrase dictionary</a>
					with four categories of register (business, academic,
					job applications, personal)</li>
				</ul>
				<p>The site interface is available in all the featured
				  languages. Quite a gold mine, and kept up to date.
				  Recommended. </p>
								  
				  <p><a href="http://www.collinsdictionary.com/">Collins Online Dictionaries</a>.
				This impressive site has bilingual (from/to English) dictionaries in
				French, Spanish, Italian, and German, as well as an English dictionary
				and Thesaurus. The detailed but clear lookup results give definitions,
				usages, and hyperlinks to related words. Additionally, and perhaps
				uniquely, there are usage examples drawn from corpora, and graphs of
				word trends over time. You can create an account on
				the site which allows you to comment on entries, and suggest new
				entries. The <a href="http://www.collinsdictionary.com/words-and-language/">Words and Language</a>
				section has interesting articles on the dictionary languages, such as
				'how to text in French' and 'social networking in Spanish'. The <a href="http://www.collinsdictionary.com/words-and-language/learning-languages/">Language Learning</a>
				section has useful articles on learning French and Spanish, including
				pronunciation guides, letter writing and idioms. Recommended.&nbsp;            </p>
				<p> <a href="http://www.dict.org/">The DICT Development
					Group</a>. A project to develop a protocol and tools to
				  develop and query online natural language databases. The
				  site currently features an interface to several online
				  English dictionaries, allowing the user to query all or
				  any of them at one time using advanced search criteria. </p>
				<p> <a href="http://www.dictionary.com">Dictionary.com</a>.
				  Online searchable English dictionary, including links to
				  other English and <a href="http://www.dictionary.com/Dir/Reference/Dictionaries/">non-English</a>
				  online dictionaries and a glossary of English jargon
				  terms. </p>
				<p> <a href="http://bank.rug.ac.be/contragram/cvvd.htm">Dutch-French-English	Contrastive Verb Valency Dictionary</a>. An "on-line
				  dictionary that contrasts the syntactic behaviour of verbs
				  which are often used as each other's translations but
				  which are invariably 'false friends', syntactically as
				  well as semantically. The CVVD systematically outlines to
				  what extent they correspond and where they differ."</p>
				<p> <a href="https://addons.mozilla.org/firefox/2444/">FoxLingo</a>.
				  A Firefox browser extension which adds translation tools
				  to the browser enabling you to translate full web pages
				  using online translation tools. At the time of writing
				  (March 2007) FoxLingo supports 45 languages.</p>
				<p><a href="http://en.glosbe.com/">Glosbe</a>.
	A multilingual dictionary, working on both crowdsourcing and scraping
	methods. It appears to get its definitions and translations from
	existing online dictionaries, with attribution and links, but it does
	solicit translations and pronunciations from users. It lists "almost
	every live language" so the word pairings run into the thousands. An
	interesting experiment and meta-search engine, though some users may
	prefer to go directly to the source(s) that Glosbe scrapes. </p>
			  <p> <a href="http://www.june29.com/IDP/">The Internet
					Dictionary Project</a>. "The Internet Dictionary
				  Project's goal is to create royalty-free translating
				  dictionaries through the help of the Internet's citizens."
				  You can search online dictionaries for English words in
				  selected European languages, or vice versa, and download a
				  Windows application allowing you to search offline. </p>
				<p> <a href="http://www.insultmonger.com/">Insultmonger</a>.
				  Includes the Swearsaurus, or "How to insult, swear, cuss,
				  and curse in 180 languages!". Surely a fine companion to
				  the long-running <a href="http://www.notam02.no/%7Ehcholm/altlang/">Alternative


					Dictionary</a>.</p>
				<p> <a href="http://www.ipl.org/ref/RR/static/ref28.00.00.html">Internet


					Public Library: Dictionaries Ready Reference</a> Links
				  to online dictionaries. </p>
				<p> <a href="http://www.itu.int/search/wais/Termite/">ITU
					Telecommunication Terminology Database (TERMITE)</a>.
				  Search the ITU database for technical terms in English,
				  French, Russian, and French. </p>
				<p><a href="http://www.langtolang.com/">Langtolang.com</a>.
				  A multilingual dictionary covering English, Italian,
				  French, German, Spanish, Russian, and Turkish. The
				  dictionary gives simple one word results. Site text in
				  English, Turkish, and German. </p>
				<p> <a href="http://www-math.uni-paderborn.de/dictionaries/Dictionaries.html">List


					of Dictionaries</a>. Primarily links to bilingual
				  (English + another language) and monolingual English
				  dictionaries, including 'informal' works such as the
				  Hacker's Dictionary, but also some non-English
				  dictionaries. </p>
				<p> <a href="http://wwli.com/translation/netglos/netglos.html">NETGLOS.</a>
				  A multilingual glossary of Internet terminology. Look up
				  Internet terms in a wide range of (mainly European)
				  languages. </p>
				<p><a href="http://www.oxfordreference.com/views/SUBJECT_SEARCH.html?subject=s16">
					Oxford Reference: Modern Languages</a>. Online,
				  searchable versions of Oxford bilingual dictionaries
				  giving full definitions and etymologies, with a useful
				  cross-reference feature to follow leads. Language pairs
				  currently served are English-French, -German, -Italian,
				  and -Spanish. </p>
				<p> <a href="http://humanities.uchicago.edu/forms_unrest/ROGET.html">Roget's


					Thesaurus.</a> An online search form for the famous
				  English thesaurus. </p>
				<p><a href="http://dictionaries.travlang.com/">travlang's
					Translating Dictionaries.</a> Online dictionaries in a
				  wide range of languages, including 'minority' languages
				  such as Danish and Dutch and even Esperanto. There's also
				  a downloadable freeware dictionary called Ergane. A highly
				  recommended site. </p>
				<p> <a href="http://www.yourdictionary.com/grammars.html">A
					Web of Online Grammars</a>. A list of links to online
				  grammars and language lessons, sorted by language. This is
				  now part of the <a href="http://www.yourdictionary.com">yourdictionary.com</a>
				  website. </p>
				<p> <a href="http://www.bemarnet.es/textil/utilities/dict.e.html">Textile
					sector dictionary</a>A searchable dictionary of textile
				  technical terms, in Spanish, English, German, and French.            </p>
				<p><a href="http://www.websters-online-dictionary.com/browse/">
					Webster's Online Dictionary - the Rosetta Edition</a>.
				  Non-searchable dictionaries/glossaries of in a wide range
				  of non-English languages, including many non-European and
				  minority languages. Entries are sorted alphabetically by
				  page. </p>
				<p> <a href="http://www.yourdictionary.com/">yourDictionary.com</a>.
				  A quite stunning collection of links to online
				  dictionaries, including speciality dictionaries, in a wide
				  range of human languages (and some non-human, such as
				  Klingon). The site also has an searchable English
				  dictionary (Webster's) and thesaurus, a translator between
				  English and four European languages, word games, grammars,
				  and much more. Recommended. (This site incorporates Robert
				  Beard's Web of Online Dictionaries.) </p>
				<p><a href="http://www.wordreference.com/">WordReference.</a>
				  An online dictionary covering English to Italian, Spanish,
				  German, and French, and vice versa. It returns full
				  definitions of words, multiple definitions when words have
				  multiple meanings (eg "present"), and phrases and
				  expressions using the word. You can also translate words
				  on a web page simply by double-clicking them. Recommended.            </p>
				  
