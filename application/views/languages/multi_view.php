				<a id="resources" name="resources"></a>
				<h3>Resource sites</h3>
				<p><a href="http://www.audio-lingua.eu/">Audiolingua</a>. A repository of freely-usable audio clips (MP3 format, streaming and download) recorded by native speakers, aimed at teachers and learners to aid oral comprehension . The clips and the site text are in multiple languages, including non-European languages such as Arabic and Mandarin, and "may be used freely for non-profit associations, personal, educational or professional purposes".</p>
				<p><a href="http://multidict.net/clilstore/">Clilstore: Teaching units for Content and Language Integrated Learning</a>.The site contains teaching materials for many languages with authentic videos and transcripts, graded for learner level, with each word linked to a choice of online dictionaries in different target languages. You can register to upload your own materials. This innovative project is part of the EU-funded POOLS-T and TOOLS programmes. </p>
				<p> <a href="http://www.lib.virginia.edu/wess/etexts.html">European Literature - Electronic Texts</a>. A collection of links to European Literature electronic texts on the WWW, sorted
				  by language. </p>
				<p><a href="http://www.freelanguagetutorials.com/">Free Language Tutorials</a>. Basic text lessons for beginners in Spanish, Italian and French. Includes some vocabulary and useful phrases. </p>
				<p> <a href="http://www.FreeTranslation.com/">FreeTranslation.com</a>.
				  A web-based machine translation service which translates
				  free text, or whole web pages, fairly quickly. At the time
				  of writing, the system can translate from English into six
				  other European languages, and from three of those
				  languages into English. </p>
				<p><a href="http://languagebox.ac.uk/">Language Box</a>. A
				  public repository of language learning materials,
				  developed by the FAROES project at the universities of
				  Southampton and Portsmouth, UK. Teachers can register for
				  an account then share their languages materials. The site
				  <q cite="http://languagebox.ac.uk/information.html">"is
					for anyone who teaches languages and who wants somewhere
					to host and manage their materials, it's also for
					language students that want to record and share good
					stuff that they've found. It is free for everybody to
					use"</q>. Language Box is free to use, and all materials
				  deposited within it are available under a Creative Commons
				  licence, although you can restrict materials that you
				  deposit to just yourself or registered users. &nbsp;</p>
				<p> <a href="http://www.language-learning.net/">Language-Learning.net</a>.
				  Information on language courses, certificates, and
				  organisations world wide. The site has a 'Course Finder',
				  via which you can carry out detailed searches for courses
				  in a particular language. </p>
				  <p><a href="http://altec.colorado.edu/languagelinks/All_Language_Resources.shtml">Multi-language apps</a>. A useful directory of mobile device apps, both multi-lingual and language-specific, including teaching aids (eg dictionaries), media (eg internet radio), news, travel, voice recording, and more. </p>
				<p> <a href="http://www.gutenberg.org/">Project Gutenberg</a>.
				  The official website of the project to make literary texts
				  available in electronic form. The project currently has
				  thousands of texts by a wide range of classical authors in
				  the original language and in translation. </p>
				<p> <a href="http://humanities.uchicago.edu/depts/romance/">The	Romance Languages Resource Page</a>. A very large
				  collection of links to sites related to Romance languages,  sorted by language. Recommended. </p>
				  <p><a href="http://sacodeyl.inf.um.es/sacodeyl-search2/">SACODEY: European Youth Language</a>. A "web based system for the assisted compilation and open distribution of European teen talk in the context of language education." Essentially its a concordancer with corpora of 'teen talk' in a number of languages - English, French, German, Italian, Lithuanian, Romanian, and Spanish - and videos. The videos require <a href="http://www.realnetworks.com/products-services/realplayer.aspx">RealPlayer</a> to view. An EU Socrates project.
				 </p>
				 <p><a href="http://www.languagecourse.net/vocabulary-trainer.php"Vocabulary trainer</a>. A 'flash card' style vocabulary teaching application, focussed on the 5000 most used words in a language. There's a range of source and target languages which you can pair, and you can choose a 'course' based on learning level or particular vocabulary (eg travel, leisure, hotels etc). All words are presented with audio, images and with progressive intervals of repetition, and the methodology looks to be based on sound pedagogical practice, at least going by the references in the FAQ. There's also an option to create your own custom flashcards 'course'. A nice technical touch is that the application is implemented solely in Javascript (jQuery.jPlayer) without any proprietary plugins.</p>
				 <p><a href="http://www.worldonlinecinema.com/Home/online-resources-listed-by-film">World Online Cinema: online resources for films</a>. Links to native language movies on DVD,, and more importantly useful resources related to the films, such as commentaries, study guides, vocabularies, exercises and more. Resources are available for movies in a range of European and non-European languages, including French, German and Iranian.</p>


