				  <a name="general"></a>
				  <h2>General</h2>
				   <p><a href="http://en.bab.la/">bab.la Language Portal</a>.
              This site has a sophisticated online dictionary with a
              good number of language pairs, including Arabic, Chinese
              and Hindi as well as the 'usual suspects'. Results are
              detailed, including usage examples, sound clips, synonyms
              and hyperlinks to related terms. The site also has:</p>
            <ul>
              <li>interactive <a href="http://en.bab.la/quiz/">quizzes</a>
                and <a href="http://en.bab.la/games/">games</a> for
                learners</li>
              <li><a href="http://en.bab.la/conjugation/">verb
                  conjugators</a> </li>
              <li><a href="http://en.bab.la/tools-plugins.php">tools and
                  plugins</a> (apps, browser plugins, etc)</li>
              <li>learner <a href="http://en.bab.la/forum/">discussion
                  fora</a></li>
              <li><a href="http://en.bab.la/phrases/">phrase dictionary</a>
                with four categories of register (business, academic,
                job applications, personal)</li>
            </ul>
            <p>The site interface is available in all the featured
              languages. Quite a gold mine, and kept up to date.
              Recommended. </p>
            <p><a href="http://www.bbc.co.uk/education/languages/">BBC
                Education - Languages</a>. Lots of online resources and
              exercises to back up BBC foreign language courses.</p>
            <p><a href="http://www.bbc.co.uk/worldservice">BBC World
                Service.</a> News, articles, and RealAudio broadcasts in
              a wide range of languages from the World Service. </p>
            <p> <a href="http://www.grass-gis.de/bibliotheken/">B&uuml;cher</a>.
              Links to online books in German, French, English, Latin,
              Greek, and Russian. </p>
            <p><a href="http://coerll.utexas.edu/coerll/">COERLL: Centre for Open Educational Resources for Language Learning</a>.
A directory of Open Educational Resources (OER) in a range of European
and non-European languages, including American Sign Language, Hindi,
Arabic, and Japanese. Hosted by the University of Texas at Austin, in
the USA. </p>
            <p><a href="http://www.answers.com/topic/list-of-country-names-in-various-languages">
                Country names in various languages</a>. A list of names
              of countries in a wide variety of languages, including
              many minority languages, sorted by the English name of the
              country. A nice little reference for when you forget, say,
              what "Sweden" is in Gaelic. </p>
            <p> <a href="http://www.dialang.org/">DIALANG.</a> A
              multinational EU project to develop diagnostic language
              assessment tools in all the EU's official languages plus
              Irish, Icelandic and Norwegian (14 languages in all). </p>
            <p><a href="http://www.lib.virginia.edu/wess/etexts.html">Electronic





                Text Collections in Western European Literature.</a>
              Page that lists Internet sources for literary texts in the
              western European languages other than English. </p>
            <p><a href="http://www.eblul.org/">European Bureau for
                Lesser Used Languages.</a> The EBLUL primarily promotes
              minority languages and cultures within Europe, lobbying
              the EU to defend and safeguard minority languages and
              communities. The site has a useful database of links to
              sites related to lesser-used languages. </p>
            <p><a href="http://www.mozilla.com/en-US/firefox/all.html">Firefox


                international versions</a>. A list of all the language
              versions of the Open Source Firefox browser.</p>
            <p> <a href="http://www.travlang.com/languages/">Foreign
                Languages for Travellers.</a> Basic words and
              expressions in a wide range of languages, both text and
              sound. You can also search for a particular word/phrase to
              be translated into all the languages. </p>
            <p><a href="http://www.heritage-education.com/article_foreignlanguageschoolresources.htm">Foreign





                Language School - Resources for College &amp; High
                School Students</a>. A small annotated list of links to
              resources for language learners, categorised by language
              (French, Italian, Spanish, Russian, German). </p>
            <p><a href="http://www.goethe-verlag.com/tests/">Goethe
                Tests</a>. Language and vocabulary tests in 16
              languages, including lesser-taught languages (eg Finnish,
              Czech, Estonian), for printing out and using in class.
              There's also a directory of language schools in the target
              languages. Site text in all the target languages. </p>
            <p><a href="http://www.comfm.com/">Guide de la Radio et des
                Medias.</a> Links to thousands of webcasting radio and
              TV websites worldwide, sorted by country and by type.
              Website text in French. </p>
            <p><a href="http://www.gedonlinediploma.com/foreign-language.html">High School Foreign Languages Resource Center</a>.
A useful collection of links to language learning resources in Chinese,
Russian, French, German, Italian, Latin and Spanish. Many of the linked
resources are themselves link collections, so to some degree this is a
site with 'pointers to pointers'. </p>
          <p> <a href="http://www.ict4lt.org">ICT4LT.</a> A
              multinational EU project to develop online training
              courses in Information and Communications Technology (ICT)
              for language teachers in the UK, Finland, and Italy. </p>
            <p> <a href="http://www.pclradio.com/">International
                Station.</a> RealAudio radio broadcasts in Polish,
              Russian, Austrian and German, Arabic, Bulgarian, and
              Serbian. <br />
            </p>
            <p><a href="http://www.intute.ac.uk/mlas/">Intute: Modern
                Languages and Area Studies</a>. Given the staggering
              amount of usable languages-related resources online, often
              the hardest task for teachers and learners is finding
              diamonds amongst the dross. Intute is a 'portal' which
              provides selected, inspected and catalogued resources in a
              very wide range of languages for educational users. You
              can browse and search for resources, and there's also a
              Virtual Training Suite offering "free tutorials on
              Internet research skills to help with your university
              course." Funding for Intute has ceased but the records
              will remain online for the foreseeable future, though
              won't be updated so expect a little linkrot. </p>
            <p><a href="http://www.uni.edu/becker/">Jim Becker's
                resources for foreign languages.</a> Includes resources
              for French, Spanish, German, Russian, Japanese, Chinese,
              Latin, Italian, and a list of best sites for language
              teachers. The text-only version is a little easier on the
              eyes than the default version.</p>
            <p><a href="http://www.languageguide.org/">Language Guide.</a>
              A useful audio vocabulary resource in a range of European,
              Semitic and Asian languages. It contains images with
              hotspots which, when you mouse over them, play vocabulary
              by native speakers. Site interface in multiple languages.
              This is a collaborative project which you can join in to
              volunteer your expertise.</p>
            <p> <a href="http://languagesmadeclear.cjb.net">Languages
                Made Clear.</a> Free online beginners courses in a
              number of (mainly European) languages, including Russian
              and Esperanto, together with detailed grammar references.
              The lessons are text only, but there are some audio clips
              in the pronunciation section. <i>Languages Made Clear</i>
              is an ongoing non-profit project which anyone who's
              willing to contribute can join as a member and help out
            with the website. </p>
            <p><a href="http://www.languagesoftware.net/">Language software reviews</a>.
Reviews of popular language learning software, sorted by language. The
packages reviewed are mainly from well-known software series - Rosetta
Stone, Natively, Transparent. You can compare packages in a language in
an at-a-glance table. As the lowest rating is 80%, and every review
ends with a 20% money off offer for the package, the reader may well
think that each page is more promotion than review, but if taken with a
pinch or three of salt this might be a useful overview site. <br />
            </p>
            <p><a href="http://www.learnalanguage.com/">Learn a Language</a>.
              Lessons, Flash games and activities in Spanish, French,
              Italian, German, Russian, Chinese and Japanese.</p>
            <p> <a href="http://www.learninggroup.com/">The Learning
                Group: Personalised Learning Games.</a> Create your own
              language learning game online (as a Java applet), or play
              games created by other authors. </p>
            <p><a href="http://carla.acad.umn.edu/lctl/lctl.html">The
                Less Commonly Taught Languages Project.</a> Resources
              for LCTLs (defined as all languages bar English, French,
              Spanish, and German). Hosted by the Centre for Advanced
              Research on Language Acquisition (CARLA). </p>
            <p><a href="http://www.linguaestrangeira.pro.br/">Lingua
                Estrangiera.</a> A large collection of teaching
              resources for Portuguese teachers of foreign languages.
              Site text in Portuguese. </p>
            <p><a href="http://www.linguanet-europa.org">Linguanet
                Europa</a>. A multilingual, virtual resources centre to
              support the teaching of foreign languages, offering access
              to a collection of over 1000 resources. Lingu@net Europa
              is accessible in Dutch, English, French, German, Italian
              and Spanish. </p>
            <p><a href="http://www.monde-diplomatique.fr/">Le Monde
                Diplomatique</a>. The French left-wing weekly paper
              online, including translations in <a href="http://www.monde-diplomatique.de/">German</a>,
              <a href="http://www.ilmanifesto.it/MondeDiplo/">Italian</a>,
              and <a href="http://www.monde-diplomatique.fr/en/">English</a>,
              with back issues online. </p>
            <p> <a href="http://www.mylanguageexchange.com">My Language
                Exchange</a>. Online community where language learners
              can find a native speaker partner to help each other
              improve foreign language skills. The site facilitates
              online language exchange learning via email, text chat and
              voice chat by providing guidelines and lesson plans. It
              claims to be one of the largest language communities on
              the web today. It also has a fun section where members can
              exchange expressions and slang by creating, playing and
              discussing phrasal Hangman word games.</p><p><a href="http://mylanguages.org">mylanguages.org</a>.
A real 'omnibus' site with language lessons in a wide range of world
languages across the continents, not just European. Many of the lessons
include audio files. A handy site for getting a basic grounding quickly
in a language.  </p>
            <p> <a href="http://wwli.com/translation/netglos/netglos.html">NETGLOS:





              </a>The Multilingual Internet Glossary Project. An online
              glossary of Internet terms in 10 different languages. </p>
            <p><a href="http://digital.library.upenn.edu/books/">The
                On-Line Books Page</a>. A searchable database of links
              to over 12,000 books online, the vast majority
              English-language. There's also a page of links to online
              libraries of non-English-language texts.&nbsp; </p>
            <p><a href="http://www.languages.dk/">Producing Open Online
                Learning Systems (POOLS)</a>. An EU project which has
              developed <a href="http://www.languages.dk/">authentic
                video materials</a> and transcripts (PDF) in 9
              languages: Basque, Danish, Dutch, English, Gaelic
              (Scottish), German, Lithuanian, Romanian, and Spanish. The
              videos are professionally-shot and available for free
              download. Recommended. </p>
            <p><a href="http://members.aol.com/dkcsac/myhomepage/romanova.htm">Project





                Romanova.</a> A new, neutral "interlanguage" for
              speakers of Romance languages, consisting of words taken
              from French, Italian, Spanish, and Portugese. The site has
              a pronunciation guide, and brief grammars and
              dictionaries. Site text in English and the Romance
              languages. </p>
            <p><a href="http://www.verbix.com/">Verbix</a>. Online
              conjugation of verbs in over 50 languages, and large
              vocabularies of verbs between language pairs (eg
              Catalan-Italian). When you get a verb conjugation (eg
              finire), you also get cross-references to
              similarly-conjugated verbs, and to equivalent verbs in
              many other languages. You can also download a trial
              version of Verbix for Windows. A very useful resource for
              language learners. Recommended. </p>
			  
