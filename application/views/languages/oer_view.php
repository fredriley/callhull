				licences that allow reuse for educational purposes, and repositories and directories of OER.</em></p>
				<a id="general" name="general"></a>
				<h3>General</h3>
				<hr />
				<a name="directories"></a>
				<h3>OER Directories</h3>
				<p><em>Directories are databases of links to OER</em>
				<p><a href="http://coerll.utexas.edu/coerll/" >COERLL: Centre for Open Educational
				Resources for Language Learning</a>. A directory of OER in a
				range of European and non-European languages, including American Sign
				Language, Hindi, Arabic, and Japanese. Hosted by the University of
				Texas at Austin, in the USA.</p>
				<hr />
				
				<a name="opencourseware" id="opencourseware"></a>
				<h3>Open Courseware</h3>
				<p><a href="http://ocw.mit.edu/">MIT Open Courseware</a>. The progenitor of the Open Courseware movement, 
				MIT allows you to find and download course materials. Languages supported: Chinese, English, French, German, 
				Japanese, Portuguese and Spanish. </p>
				<p><a href="http://www.open.edu/openlearn/">Openlearn</a>. This is a groundbreaking and innovative project from the UK Open University. It's a full learning environment where you can access all OU course materials, build your own learning programme by mixing modules, and get actively involved in learner fora and clubs. There's even a videoconferencing tool so that you can talk to other students. The <a href="http://www.open.edu/openlearn/languages">Languages section</a> has Chinese, English, Gaelic, Welsh, French, Italian, German and Spanish. Openlearn is far more than an 	open courseware repository, and is far and away the best open courseware site in UK Higher Education. 
				Highly recommended.</p>
				<p><a href="http://www.onlinecourses.com/language/">Online language courses</a>. Structured language courses delivered as Massive Open Online Courses (MOOCs), taught by academics at respected higher education institutions. Registration is free and open to all. Part of a larger <a href="http://www.onlinecourses.com/">online courses site</a> delivering courses across a wide range of subject areas. The site is designed to be mobile-friendly, but that may not be the case with all the courses it lists. </p>
				
				<hr />
				
				<a id="repositories" name="repositories"></a>
				<h3>OER repositories</h3>
				<p><em>OER repositories contain downloadable resources</em></p>
				<p><a href="http://languagebox.ac.uk/">Languages Box</a>. A contributory repository of OER languages resources, hosted at the 
					University of Southampton, UK. Resources include documents, presentations, quizzes and websites. You can create an account 
					to contribute your resources to the repository. </p>
				<p><a href="http://loro.open.ac.uk/">LORO: Languages Open Resources Online</a>.
				A large, contributory repository of language resources for teaching and
				learning, run by the Open University, which includes OU course
				materials. You can register in order to create your own profile, manage
				your resources, bookmark and save your searches.</p>
