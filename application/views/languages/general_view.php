				<p><a id="linguistics" name="linguistics"></a> </p>
				<h3>Linguistics</h3>
				<p><a href="http://www.cs.kun.nl/agfl/">The AGFL Grammar
					Work Lab</a>. Downloadable software for natural language
				  processing, based on the AGFL (Affix Grammars over Finite
				  Lattices) system developed at the University of Nijmegen.            </p>
				<p> <a href="http://www.scs.leeds.ac.uk/amalgam/amalgam/amalghome.htm">AMALGAM</a>
				  Automatic Mapping Among Lexico-Grammatical Annotation
				  Models. </p>
				<p><a href="http://linguistlist.org/%7Eask-ling/">Ask-a-Linguist</a>.
				  A service provided by the <a href="http://linguistlist.org/">LINGUIST</a>
				  discussion list, Ask-a-Linguist allows anyone to submit
				  any question relating to languages or linguistics to a
				  panel of volunteer professional linguists.</p>
				<p> <a href="http://www.scs.leeds.ac.uk/amalgam/ccalas/homepage.html">CCALAS</a>
				  Centre for Computer Analysis of Language and Speech. </p>
				<p> <a href="http://www.cal.org/">Centre for Applied
					Linguistics.</a> A vast collection of resources for
				  those interested in "applying research information about
				  language and culture to educational, cultural, and social
				  concerns", including papers, databases, clearing houses,
				  and links. </p>
				<p> <a href="http://bank.rug.ac.be/contragram/">Contragram</a>.
				  A research group on contrastive grammar at the University
				  of Gent, Belgium. Resources on the website include the
				  Contragram newsletters, a bibliography on contrastive
				  linguistics, and a Dutch-French-English Contrastive Verb
				  Valency Dictionary. </p>
				<p><a href="http://www.eiti.com/country_language_lookup.cfm">
				  </a><a href="http://www.ethnologue.com/">Ethnologue:
					Languages of the World</a> A comprehensive reference on
				  over 6,700 world languages, compiled by <a href="http://www.sil.org/">SIL</a>.
				  The brief entry for each language, includes its alternate
				  names, number of speakers, location, and dialects. </p>
				<p><a href="http://www.fonetiks.org">fonetiks.org</a>. An
				  online pronunciation guide with audio clips for selected
				  languages, primarily English and national variants but
				  also Spanish, French, Italian, German, Thai, and
				  Indonesian. The pages take a little while to load as the
				  audio clips (.wav) are embedded in them, and Macs won't be
				  able to play the clips as they use Windows Media Player. </p>
				<p><a href="http://www.qualityansweringservice.com/resources/depth-look-language">In-depth


					look at language</a>. A clearly-written short primer for
				  those new to Linguistics and the structure of languages,
				  incorporating links to linguistics resources. </p>
				<p><a href="http://www.linguistsfederation.com/">Linguists
					Federation</a>. The Federation promotes knowledge of
				  languages by<br />
				  organising meetings worldwide to practise languages,
				  usually through the medium of informal clubs and circles.
				  The website lists existing circles and clubs by country
				  and town, with contact details for anyone wishing to join
				  in.</p>
				<p><a href="http://ocw.mit.edu/global/department24.html">MIT
					Open Courseware | Linguistics and Philosophy</a>. The
				  purpose of the MIT Open Courseware project is to <q cite="http://ocw.mit.edu/global/about-ocw.html">provide


					free, searchable, coherent access to MIT's course
					materials for educators in the non-profit sector,
					students, and individual learners around the world</q>.
				  At the time of writing (March 2003) the Linguistics &amp;
				  Philosophy section has two linguistics courses available,
				  including <a href="http://ocw.mit.edu/24/24.900/s02/index.html">Introduction


					to Linguistics</a>. </p>
				<p> <a href="http://www.omniglot.com/">Omniglot - a guide
					to writing systems</a>. An encyclopaedic and detailed
				  guide to over 200 different alphabets, syllabaries and
				  other writing systems, including selected links to further
				  resources about each system. There's also a selection of
				  languages links, a bibliography, a small number of
				  downloadable fonts, and an especially useful section on <a href="http://www.omniglot.com/mlcomputing/">multilingual


					computing</a> dealing with the perennial issue of
				  displaying and writing in non-Roman character sets. Behind
				  a deceptively quiet home page this site conceals a wealth
				  of resources for linguists. Recommended. </p>
				<p> <a href="http://www.worldnet.net/%7Exmd/Resources_Linguistiques.html">Resources


					Linguistiques</a> A long list of links to
				  language-related sites, written in French. </p>
				<p> <a href="http://www.facstaff.bucknell.edu/rbeard/index.html">Robert


					Beard's Linguistics Page </a>Includes online
				  dictionaries and a list of morphology sites. </p>
				<p><a href="http://www.rosettaproject.org"> Rosetta Project</a>.
				  A global collaborative project intended to create a
				  permanent archive of over 1,000 threatened or dead
				  languages. Each language has a section with a linguistic
				  description, links to resources on the language, and texts
				  in the language. </p>
				<p><a href="http://classweb.gmu.edu/accent/"> Speech accent
					archive</a>. A large database of the accentuated speech
				  of native and non-native English speakers from a wide
				  range of origins, reading the same English paragraph, with
				  phonetic transcription. </p>
				<p> <a href="http://www.sil.org/">Summer Institute of
					Linguistics (SIL)</a> Resources related to "the study of
				  minority languages and cultures around the world." </p>
				<p><a href="http://textalyser.net/">Textalyser</a>. An
				  online text analyser which will analyse blocks of pasted
				  text, or a web page from a URL, and parse it to obtain
				  statistics such as word count, average syllable length,
				  readability, word frequency, etc. A handy little utility.</p>
				<p> <a href="http://www.intext.de/">Text analysis software
					(Intext) </a>. Public domain text analysis software
				  which runs in 32-bit MSDOS, and is downloadable from the
				  site. Site text in English and German. </p>
				<p> <a href="http://www.phon.ucl.ac.uk/">University College
					London Department of Phonetics and Linguistics</a>. A
				  host of linguistics resources, including web tutorials in
				  phonetics written by students. </p>

