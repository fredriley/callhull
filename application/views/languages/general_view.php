					Institute of Linguistics</a>. </p>
				<p> <a href="http://www.sil.org/computing/fonts/encore-ipa.html">Phonetic


					Fonts.</a> Summer Institute of Linguistics (<a href="http://www.sil.org/">SIL</a>)
				  IPA fonts for Windows and Macintosh. As well as the fonts
				  there's documentation on how to install and use them. </p>
