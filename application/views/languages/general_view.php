				<h3><a id="journals" name="journals"></a>Journals</h3>
				<p><b><i>NB:</i></b><i> This section contains journals that
					aren't dedicated to a particular language.
					Language-specific journals can be found in the
					appropriate language section.</i></p>
				<p><a href="http://cleo.murdoch.edu.au/ajet/">Australian
					Journal of Educational Technology.</a> A tri-annual,
				  refereed journal. The website has back issues in HTML,
				  with a search facility. </p>
				<p> <a href="http://llt.msu.edu">Language Learning and
					Technology</a>. A refereed journal for second and
				  foreign language educators. </p>
				<p><a href="http://www.languagemagazine.com/">Language
					Magazine</a>. A bimonthly magazine for language teachers
				  in the US, focussing primarily, but not exclusively, on
				  ESL. There are back issues, book reviews, and conference
				  reports online, plus a links page and even a section
				  devoted to language funnies. Formerly <i>American
					Language Online.</i> </p>
				<p><a href="http://nflrc.hawaii.edu/rfl">Reading in a
					Foreign Language</a>. A refereed journal published by
				  the <a href="http://nflrc.hawaii.edu/">National Foreign
					Language Resource Center</a> at the University of
				  Hawaii. Current and back issues online in PDF. </p>
