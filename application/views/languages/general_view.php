				<h3>Discussion Lists and Newsgroups </h3>
				<p> <a href="http://n2h2.com/KOVACS/">The Directory of
					Scholarly and Professional E-Conferences</a>. Search for
				  a discussion list by keywords. </p>
				<p> <a href="http://www.evertype.com/langlist.html">Everson's


					and Comrie's List of Language Lists</a>. A
				  comprehensive, long-established list of email discussion
				  lists related to languages and linguistics. </p>
				<p> <a href="http://www.harley.com/usenet/">Harley Hahn's
					Usenet Newsgroup list.</a> A searchable and browsable
				  list of newsgroups on Usenet. </p>
				<p><a href="http://www.jiscmail.ac.uk">Jiscmail</a>.
				  Discussion lists for UK Higher Education, many of which
				  are also open to people outside this sector. You can
				  search for lists by keyword. </p>
				<p> <a href="https://maillists.uci.edu/mailman/listinfo/papyrus-news">Papyrus


					News</a>. A distribution list relating to "the global
				  impact of information technology on language, literacy,
				  and education", run by <a href="http://www.gse.uci.edu/markw/">Mark


					Warschauer</a> of the University of Hawaii. </p>
				<p> <a href="http://languagecenter.cla.umn.edu/teachtech/">TeachTech</a>.
				  A listserv for teachers of language teachers and the use
				  of technology in second language learning. </p>
