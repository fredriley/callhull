				<h3>Directories of language resources</h3>
				<p><b>NB: </b><i>The links in this section relate to
					multi-language directories - those specific to a
					language can be found in the appropriate language
					section of this list.</i> </p>
				<p><a href="http://www.bbc.co.uk/voices/webguide/">BBC
					Webguide to Languages and Linguistics</a>. A short,
				  annotated list of links to useful sites on languages and
				  linguistics. </p>
				<p><a href="http://www.call4all.us/">CALL 4 All</a>.
				  Described as a <q cite="http://www.call4all.us/">"A
					Virtual Encyclopedia of all major language learning
					links, Web dictionaries and Computer-Assisted Language
					Learning organizations in the world"</q>, this site aims
				  to be the definitive compendium of links to sites and
				  resources in Computer-Aided Language Learning (CALL).
				  Links are briefly annotated, and sorted alphabetically by
				  title.</p>
				<p> <a href="http://www.rgu.ac.uk/%7Esim/research/netlearn/foreign.htm">Foreign


					Language Resources at Netlearn.</a> A short, but
				  well-annotated, list of resources, including Internet
				  Guides in a number of languages (particularly French). </p>
				<p> <a href="http://www.ilovelanguages.com/">The Human
					Languages Page</a>. (aka "I Love Languages".) A resource
				  containing pointers to tutorials, dictionaries, and even
				  audio files in a wide range of languages, including some
				  lesser-known such as Serbo-Croat and Gaelic. One of the
				  most comprehensive collections of language sites on the
				  WWW. Recommended. </p>
				<p> <a href="http://atschool.eduweb.co.uk/stpmlang/index.html">Language


					Links.</a> A list hosted by the Modern Languages
				  Department of St. Peter's School, York, UK. </p>
				<p><a href="http://www.linguanet-europa.org"> Lingu@net
					Europa</a>. A database of Internet resources for
				  education and training, organised by language. Site text
				  in Dutch, English, French, German, Italian and Spanish.
				  Lingu@net is an EU initiative. </p>
				<p> <a href="http://www.linguanet.org.uk/">Lingu@NET at
					NCET</a> "A one-stop language centre". Pointers to
				  language resources. </p>
				<p> <a href="http://www.searchlanguage.com/">Search
					Languages: Language Resource Directory</a>. A Yahoo-like
				  searchable directory of language resources on the Net,
				  sorted by category, including resources in Arabic and
				  Chinese. </p>
				<p> <a href="http://www.ced.ufsc.br/%7Euriel/">URIEL</a>.
				  Italian, Spanish, French, English, and German links. </p>
				<p> <a href="http://www.virtualtourist.com/">Virtual
					Tourist Pre-Home Page</a> Comprehensive guide to all the
				  WWW servers in the world, and local and regional
				  information. </p>
				<p><a href="http://www.netword.com/esl_home">Web Resources
					for CALL Lab Managers.</a> An annotated and categorised
				  collection of links particularly aimed at managers of CALL
				  Laboratories (eg training, setting up labs) and ESL
				  teachers. </p>
				<p> <a href="http://www.yahoo.com/Social_Science/Linguistics_and_Human_Languages/Languages/Education/">Yahoo!


					Social Science:Linguistics and Human
					Languages:Languages:Education</a> </p>
				<p> <a href="http://www.yahoo.com/Regional/Countries">Yahoo
					subject tree: Countries</a> WWW sites in countries
				  worldwide sorted by category. </p>
